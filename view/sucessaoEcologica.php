            <h1><u>Sucessão Ecológica</u></h1>

            <p>A <strong>sucessão ecológica</strong> é o processo gradual de mudanças da estrutura e composição de uma comunidade.</p>

            <p>Representa um processo ordenado de mudanças no ecossistema, incluindo alterações no ambiente físico pela comunidade biológica, até alcançar o fase de clímax.</p>

            <p>Durante a sucessão ecológica, as comunidades mais simples vão com o passar do tempo sendo substituídas por comunidades mais complexas.</p>

            <p>A sucessão ecológica passa por três fases: a <strong>ecese, seral e clímax</strong>.</p>

            <p>A ecese representa a comunidade pioneira. São os primeiros organismos a se instalarem no ambiente, como líquens, gramíneas e insetos.</p>

            <p>A seral é a comunidade intermediária. Representada pela vegetação de pequeno porte, arbustiva e herbácea. Nessa fase ocorrem mudanças significativas na comunidade.</p>

            <p>A última fase é o clímax, a comunidade estabilizada. A comunidade atinge elevado número de espécies, os nichos ecológicos são ocupados e apresenta grande quantidade de biomassa.</p>

            <p>A comunidade tende a evoluir até clímax, quando é formada por populações em equilíbrio com o meio.</p>

