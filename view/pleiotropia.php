            <h1><u>Pleiotropia</u></h1>

            <p><strong>Pleiotropia</strong> ocorre quando um par de alelos condiciona mais de um caráter. Assim, um <strong>único gene controla diversas características do fenótipo</strong> que em alguns casos não estão relacionadas.</p>

            <p>O gene responsável pela pleiotropia denomina-se <strong>pleiotrópico</strong>.</p>


            <img src="https://s1.static.brasilescola.uol.com.br/be/e/pleiotropia.jpg" alt="" style="width: 50%;">
            <h2>Pleiotropia em Humanos</h2>

            <p>Um exemplo de gene pleiotrópico em humano é o que causa a Síndrome de Marfan.</p>

            <p>Os portadores do alelo dominante que ocasiona a síndrome apresentam aracnodactilia, dedos longos, finos e curvos. Além de anomalias ósseas e problemas nos olhos, coração e pulmões.</p>

            <p>Outro exemplo é a fenilcetonúria. Nesse caso, ocorre uma mutação no <strong>gene</strong> que codifica a enzima fenilalanina hidroxilase, responsável por degradar o aminoácido fenilalanina.</p>

            <p>Os acometidos com fenilcetonúria apresentam incapacidade mental, redução de pelos e na pigmentação da pele, convulsões e movimentos incontroláveis nas pernas e braços.</p>

            <h2>Pleiotropia e Interação Gênica</h2>

            <p>Cada um dos fenômenos diferem entre si:</p>

            <p>• Na pleiotropia um único gene controla diversas características do fenótipo.</p>
            <p>• Na <strong>interação gênica</strong>, dois ou mais genes interagem e controlam apenas uma característica. Podemos dizer que a <strong>pleiotropia é o inverso da interação gênica</strong>.</p>

