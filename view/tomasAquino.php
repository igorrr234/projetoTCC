            <h2>Biografia</h2>

            <p>Tomás de Aquino nasceu em 1225, em Aquino, uma comuna italiana, no Castelo de Roccasecca.</p>

            <p>Filho do Conde Landulf de Aquino, recebeu uma excelente educação. Estudou na abadia de Roccasecca, no Mosteiro da Ordem de São Bento de Cassino. Mais tarde, ingressou na Universidade de Nápoles, na Cátedra “Artes Liberais”.</p>

            <p>Com apenas 19 anos, em 1244, abandona o curso e decide seguir sua vocação religiosa tornando-se dominicano, ao ingressar na Ordem dos Dominicanos, no convento Saint Jacques, em Paris. Permaneceu alguns anos em Paris, cidade importante para seu desenvolvimento espiritual, intelectual e profissional.</p>

            <p>Entretanto, foi na cidade de Colônia, na Alemanha, que Aquino escreve suas primeiras obras, sendo discípulo do bispo, filósofo e teólogo alemão Santo Alberto Magno (1206 d.C-1280 d.C.), conhecido como Alberto, o grande.</p>

            <p>Mais tarde, em 1252, Tomás de Aquino retorna a Paris onde se gradua em Teologia e segue a carreira de professor. Ministrou aulas em Roma, Nápoles e outras cidades da Itália.</p>

            <p>Ficou conhecido como Doutor Angélico, cujo trabalho de vida esteve dedicado a fé, a esperança e a caridade constituindo assim, um pregador cristão da razão e da prudência.</p>

            <p>Foi um dos defensores da <strong>Escolástica</strong>, método dialético que pretendia unir a fé a razão em prol do crescimento humano. Uma de suas maiores obras, Summa Theologica, é o maior exemplo da Escolástica, na qual apresenta relações entre a ciência, razão, filosofia, fé e teologia. Segundo Aquino:</p>
            <br>
            <p><strong>“Nada há no intelecto que antes não tenha passado pelos sentidos.”</strong></p>
            <br>
            <p>São Tomás Faleceu na cidade de Fossanova, Itália, no dia 7 de março de 1274, com 49 anos.</p>

            <h2>Filosofia de São Tomás de Aquino</h2>

            <p>Inspirado nas ideias do filósofo grego <strong>Aristóteles</strong> (384 a.C.-322 a.C.), o trabalho de São Tomás de Aquino esteve pautado no realismo aristotélico, em detrimento dos seguidores de Santo Agostinho, inspirados no idealismo de Platão.</p>

            Por isso, Aquino foi um dos pensadores mais destacados desse período, defensor da filosofia escolástica, método cristão e filosófico, pautado na união entre a razão e a fé.</p>

            <p>Assim, Tomás de Aquino escreveu inúmeras obras, onde privilegiou a razão e a vontade humana formulando assim, um novo pensamento filosófico cristão.</p>

