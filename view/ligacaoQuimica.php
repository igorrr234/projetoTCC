            <h1><u>Ligações Químicas</u></h1>

            <p>As ligações químicas correspondem à <strong>união dos átomos</strong> para a formação das substâncias químicas.</p>

            <p>Em outras palavras, as ligações químicas acontecem quando os átomos dos elementos químicos se combinam uns com os outros e os principais tipos são:</p>

            <p><strong>• Ligações iônicas</strong>: transferência de elétrons;</p>
            <p><strong>• Ligações covalentes</strong>: compartilhamento de elétrons;</p>
            <p><strong>• Ligações metálicas</strong>: existência de elétrons livres.</p>

            <h2>Regra do Octeto</h2>

            <p>A Teoria do Octeto, criada por Gilbert Newton Lewis (1875-1946), químico estadunidense, e Walter Kossel (1888-1956), físico alemão, surgiu a partir da observação dos gases nobres e algumas características como, por exemplo, a estabilidade dos elementos que apresentam 8 elétrons na Camada de Valência.</p>

            <p>Portanto, a Teoria ou Regra do Octeto explica a ocorrência das ligações químicas da seguinte forma:</p>
            <br>
            <p><strong>“Muitos átomos apresentam estabilidade eletrônica quando possuem <strong>8 elétrons na camada de valência</strong> (camada eletrônica mais externa).”</strong></p>
            <br>
            <p>Para tanto, o átomo procura sua estabilidade doando ou compartilhando elétrons com outros átomos, donde surgem as ligações químicas.</p>

            <p>Vale lembrar que existem muitas exceções à Regra do Octeto, principalmente entre os elementos de transição.</p>
            <h2>Tipos de Ligações Químicas</h2>

            <h3>Ligação Iônica</h3>

            <p>Também chamada de <strong>ligação eletrovalente</strong>, esse tipo de ligação é realizada entre íons (cátions e ânions), daí o termo "ligação iônica".</p>

            <p>Para ocorrer uma ligação iônica os átomos envolvidos apresentam tendências opostas: um átomo deve ter a capacidade de perder elétrons enquanto o outro tende a recebê-los.</p>

            <p>Portanto, um ânion, de carga negativa, se une com um cátion, de carga positiva, formando um composto iônico por meio da interação eletrostática existente entre eles.</p>

            <p><strong>Exemplo</strong>: Na+Cl- = NaCl (cloreto de sódio ou sal de cozinha). Nesse composto, o sódio (Na) doa um elétron para o cloro (Cl) e se torna um cátion (carga positiva), enquanto o cloro torna-se um ânion (carga negativa).</p>

            <img src="https://static.todamateria.com.br/upload/li/ga/ligacaoionicaexemplocloretodesodionacl-cke.jpg" alt="" style="width: 60%;">

            <p> Outros exemplos de substâncias formadas por ligações iônicas são:</p>

            <p>• Brometo de potássio, KBr</p>
            <p>• Cloreto de cálcio, CaCl2</p>
            <p>• Fluoreto de magnésio, MgF2</p>
            <p>Os compostos iônicos geralmente são encontrados no estado sólido em condições ambientes e apresentam elevados pontos de fusão e ebulição. Quando dissolvidos em água, essas substâncias são capazes de conduzir corrente elétrica, já que seus íons são liberados em solução.</p>

            <h3>Ligação Covalente</h3>

            <p>Também chamada de <strong>ligação molecular</strong>, as ligações covalentes são ligações em que ocorre o compartilhamento de elétrons para a formação de moléculas estáveis, segundo a Teoria do Octeto; diferentemente das ligações iônicas, em que há perda ou ganho de elétrons.</p>

            <p>Além disso, pares eletrônicos é o nome dado aos elétrons cedidos por cada um dos núcleos, figurando o compartilhamento dos elétrons das ligações covalentes.</p>

            <p><strong>Exemplo</strong>: H2O: H - O - H (molécula de água) formada por dois átomos de hidrogênio e um de oxigênio. Cada traço corresponde a um par de elétrons compartilhado formando uma molécula neutra, uma vez que não há perda nem ganho de elétrons nesse tipo de ligação.</p>

            <img src="https://static.todamateria.com.br/upload/li/ga/ligacaocovalenteexemploaguah2o-cke.jpg?auto_optimize=low" alt="" style="width: 60%;">


            <p>Outros exemplos de substâncias formadas por ligações covalentes são:</p>

            <p>• Gás oxigênio, O2</p>
            <p>• Sacarose (açúcar de mesa), C12H22O11</p>
            <p>• Ácido clorídrico, HCl</p>
            <p>As ligações covalentes podem ser classificadas em polares ou apolares. No caso da água temos uma <strong>ligação covalente polar</strong>, pois os átomos que compõem a molécula apresentam diferentes eletronegatividades. Já o oxigênio (O2) apresenta uma <strong>ligação covalente apolar</strong>, pois é formado por átomos de um único elemento químico e, por isso, não apresenta diferença de eletronegatividade.</p>

            <h3>Ligação Metálica</h3>


            <p>É a ligação que ocorre entre os metais, elementos considerados eletropositivos e bons condutores térmico e elétrico. Para tanto, alguns metais perdem elétrons da sua última camada chamados de "elétrons livres" formando assim, os cátions.</p>

            <p>A partir disso, os elétrons liberados na ligação metálica formam uma "nuvem eletrônica", também chamada de "mar de elétrons" que produz uma força fazendo com que os átomos do metal permaneçam unidos.</p>

            <img src="https://static.todamateria.com.br/upload/li/ga/ligacaometalicaexemploeletronseionsmetalicos-cke.jpg?auto_optimize=low" alt="">

            <p>Exemplos de metais: Ouro (Au), Cobre (Cu), Prata (Ag), Ferro (Fe), Níquel (Ni), Alumínio (Al), Chumbo (Pb), Zinco (Zn), entre outros.</p>

            <p>Os metais apresentam estado físico sólido em temperatura ambiente, com exceção do mercúrio, o único metal líquido nessas condições. As substâncias metálicas são boas condutoras de calor e eletricidade e, além disso, apresentam um brilho característico.</p>


