      <h1><u>Guerra Fria</u></h1>

      <p>A <strong>Guerra Fria</strong> foi uma luta político-militar entre o socialismo e o capitalismo liderada pela União Soviética e Estados Unidos.</p>

      <p>Esta conflagração teve início após a Segunda Guerra Mundial (1939-1945), mais precisamente em 1947, quando o presidente americano Henry Truman fez um discurso no Congresso americano, afirmando que os Estados Unidos poderiam intervir em governos não democráticos.</p>

      <p>Esta época ficou assim conhecida porque ambos os países nunca se enfrentaram diretamente num conflito bélico.</p>

      <p>O fim da Guerra Fria é marcado pela dissolução e fim da URSS, em 1991. Os Estados Unidos saíram vencedores deste conflito.</p>

      <img src="https://static.todamateria.com.br/upload/gu/er/guerra-fria-og.jpg?class=ogImageRectangle" alt="" style="width: 65%;">

      <h2>Início da Guerra Fria (1947) e suas causas</h2>

      <p>Em 1947, visando combater o comunismo e a influência soviética, o presidente americano Harry Truman proferiu um discurso no Congresso americano. Nele, afirmou que os Estados Unidos se posicionariam a favor das nações livres que desejassem resistir às tentativas de dominação externa. A dominação a que ele se referia era ao crescimento da União Soviética e sua influência em novos territórios.</p>

      <p>No mesmo ano, o secretário de Estado americano, George Marshall, lançou o Plano Marshall, que propunha a ajuda econômica aos países da Europa Ocidental. Esse plano surgiu em um contexto onde os partidos de esquerda cresciam devido ao desemprego e a crise generalizada.</p>

      <p>Assim, os Estados Unidos temiam que cada vez mais revoluções socialistas levassem os norte-americanos a perder áreas de influência para a URSS.</p>

      <p>Como resposta, a União Soviética criou o Kominform, organismo encarregado de conseguir a união dos principais partidos comunistas europeus. Também era sua tarefa afastar da supremacia norte-americana os países sob sua influência, gerando o bloco da “cortina de ferro”.</p>

      <p>Além disso, foi criado em 1949, o Comecon, uma espécie de Plano Marshall para os países socialistas.</p>

      <h2>Expansão da Guerra Fria</h2>

      <p>Ao fim das negociações entre os vencedores da Segunda Guerra Mundial, a Europa ficou dividida em duas partes. Estas correspondiam ao limite do avanço de tropas soviéticas e americanas durante a guerra.</p>


      <p> A parte oriental, ocupada pelos soviéticos, tornou-se área de influência da União Soviética.</p>

      <p>Os partidos comunistas locais, apoiados pela URSS, passaram a exercer o poder nesses países. Estabeleceram as chamadas democracias populares na Albânia, Romênia, Bulgária, Hungria, Polônia e Checoslováquia.</p>

      <p>Na Europa, somente a Iugoslávia estabeleceu um regime socialista independente da União Soviética.</p>

      <p>Por outro lado, a parte ocidental, ocupada principalmente por tropas inglesas e norte-americanas, ficou sob a influência dos Estados Unidos. Nessa área, consolidaram-se democracias liberais, com exceção das ditaduras na Espanha e em Portugal.</p>

      <p>As duas superpotências buscavam ampliar suas áreas de influência no mundo, intervindo direta ou indiretamente nos assuntos internos destes países.</p>

      <h3>OTAN e o Pacto de Varsóvia</h3>

      <p>A Guerra Fria provocou a formação de duas alianças politico-militares:</p>
      <br>
      <p>• A Organização do Tratado do Atlântico Norte (OTAN);</p>
      <p>• O Pacto de Varsóvia.</p>
      <br>
      <p>A OTAN, fundada em 1949, era composta inicialmente por Estados Unidos, Canadá, Reino Unido, França, Bélgica, Países Baixos, Luxemburgo, Dinamarca, Noruega, Finlândia, Portugal e Itália. Mais tarde juntaram-se Alemanha Ocidental, Grécia e Turquia, opondo toda a Europa Ocidental à União Soviética.</p>


      <p>Em 1955, em represália, a União Soviética criou o Pacto de Varsóvia, para impedir o avanço capitalista na sua área de influência. No ano de sua fundação faziam parte URSS, Albânia, Alemanha Oriental, Bulgária, Tchecoslováquia, Hungria, Polônia e Romênia.</p>

      <p>Os dois pactos tinham em comum o compromisso de mútua proteção entre seus membros, pois entendiam que a agressão a um deles atingiria a todos.</p>

      <p>O Pacto de Varsóvia desapareceu entre 1990 e 1991, em consequência do fim dos regimes socialista do Leste europeu. Do outro lado, a OTAN perdeu o significado que lhe deu origem.</p>

      <h3>Disputas na Guerra Fria</h3>

      <p>No início dos anos 60, a construção do <strong>Muro de Berlim</strong>, em 1961; e a crise dos mísseis, em 1962, provocaram o aumento das tensões internacionais.</p>

      <h3>Muro de Berlim (1961)</h3>

      <p>A construção do Muro de Berlim, em 1961, dividiu a cidade de Berlim entre Berlim Ocidental e Berlim Oriental.</p>

      <p>O objetivo era impedir a saída de profissionais e trabalhadores qualificados que deixavam a socialista Alemanha Oriental em busca de melhores condições de vida na capitalista Alemanha Ocidental.</p>

      <h3>Crise dos Mísseis (1962)</h3>

      <p>A crise dos mísseis teve origem na pretensão soviética de instalar bases e lançamento de mísseis em Cuba. Se isto se concretizasse, seria uma ameaça constante para os Estados Unidos.</p>

      <p>A reação americana se fez imediata, através de um bloqueio naval sobre Cuba, o único país da América que havia adotado o regime socialista. O mundo conteve a respiração, pois nesse momento, as chances de uma Terceira Guerra Mundial eram reais.</p>

      <p>As negociações foram tensas, mas os soviéticos desistiram de colocar os mísseis em Cuba. Em troca, os Estados Unidos fizeram o mesmo nas suas bases na Turquia, seis meses depois.</p>

      <h3>Corrida Espacial</h3>

      <p>Outra característica da Guerra Fria foi a Corrida Espacial, iniciada no final da década de 50.</p>

      <p>Muito dinheiro, tempo e estudo foram investidos pela URSS e pelos EUA para saber quem dominaria a órbita terrestre e o espaço.</p>

      <p>Os soviéticos saíram na frente, em 1957, com os satélites Sputnik, mas os americanos os alcançaram e fizeram o primeiro homem caminhar em solo lunar, em 1969.</p>

      <p>A corrida espacial não incluía somente o objetivo de levar pessoas ao espaço. Também fazia parte do projeto desenvolver armas de longo alcance, como mísseis intercontinentais e escudos espaciais.</p>

      <h2>O fim da Guerra Fria (1991)</h2>

      <p>Historiadores atribuem o fim da Guerra Fria ao fim da União Soviética, em 1991. Este antecedeu outro evento marcante do período, que foi a Queda do Muro de Berlim, em 1989.</p>

      <p>O conflito ideológico só foi terminado graças às negociações estabelecidas por Ronald Reagan e Mikahil Gorbachev durante a década de 80.</p>

      <p>A queda do Muro de Berlim foi o marco visível que simbolizou o fim dos regimes socialistas no Leste europeu. Após sua derrubada, os regimes socialistas foram caindo um a um, e em outubro de 1990, as duas Alemanhas foram finalmente unificadas.</p>

      <p>Igualmente, a desintegração da União Soviética, em 1991, inaugurou um período na história mundial, dando início ao processo de implantação do capitalismo em todos os países do globo.</p>

      <h2>Consequências da Guerra Fria</h2>

      <p>Na economia, o fim da Guerra Fria iniciou a expansão do capitalismo a todos os países do globo.</p>

      <p>O mundo abandonou as disputas ideológicas das décadas anteriores para se concentrar em apenas em uma ideologia, a capitalista. Nesta fase, o capitalismo assumiu o nome de neoliberalismo, onde o Estado deve intervir o mínimo possível na economia.</p>

      <p>Já com a desintegração da União Soviética surgiram quinze novos países. Na Europa, observamos a separação da Tchecoslováquia e o início da Guerra da Iugoslávia.</p>

      <p>As instituições lideradas pela União Soviética desapareceram. O Pacto de Varsóvia acabou entre 1990 e 1991, em consequência do fim dos regimes socialista do Leste europeu.</p>

      <p>A própria OTAN perdeu o significado que lhe deu origem e agora é uma aliança militar utilizada para o combate ao terrorismo.</p>

      <p>Alguns resquícios da Guerra Fria no mundo atual são a separação da Coreia do Norte e do Sul, a existência de ogivas nucleares americanas em bases da Alemanha e a tensão nas relações entre a Rússia e os Estados Unidos.</p>



