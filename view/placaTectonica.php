      <h1><u>Placas Tectônicas</u></h1>

      <p>As <strong>placas tectônicas</strong> são porções da camada externa da estrutura terrestre chamada de litosfera, onde estão localizados os continentes e oceanos.</p>

      <p>Essas placas tectônicas movem-se sobre a camada inferior mais fluida, chamada de astenosfera.</p>

      <p>A camada da superfície terrestre é constituída por sete principais placas rochosas rígidas que mudam de posição e se encaixam como peças de um quebra-cabeça.</p>

      <p>O movimento dessas placas podem ser convergentes, quando se movem uma contra a outra; divergentes, quando se afastam ou conservativo, quando se movem vertical ou paralelamente.</p>

      <p>Ao movimento das placas são responsáveis pelos vulcões, terremotos e tsunamis. Assim como a formação dos continentes e mares, formação de cadeias de montanhas e de toda a paisagem que se situa sobre essas placas tectônicas.</p>

