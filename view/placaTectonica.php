      <h2>O Movimento das Placas Tectônicas</h2>

      <p>Os movimentos das placas tectônicas são responsáveis por uma série de acidentes geográficos, tais como: vulcões, terremotos e tsunamis.</p>

      <p>O movimento das placas também foi o responsável pela formação dos continentes e a definição do mapa da Terra, tal como se conhece.</p>

      <p>Alguns indícios como a semelhança entre as costas atlânticas do continente africano e sul-americano e fósseis de diversas espécies comuns de ambos os lados levam a crer que o planeta já foi formado por um único continente, chamado de Pangeia, há cerca de 225 milhões de anos.</p>

      <p>Os movimentos das placas tectônicas podem ser observados através de seus limites e são classificados como:</p>
      <br>
      <p>• Divergentes (que definem a zona de construção da crosta),</p>
      <p>• Convergentes (definidas na zona de destruição da crosta) e</p>
      <p>• Conservativos (onde estão as falhas transformantes).</p>

      <h3>Movimentos divergentes das placas tectônicas</h3>

      <p>Ocorre quando as placas traçam o movimento de distância uma das outras provocando o “nascimento” de uma nova crosta oceânica.</p>

      <p>O movimento é traçado no sentido horizontal. Esse limite é definido em três estágios, sendo o primeiro a abertura de uma fenda que ocorre com a fratura da crosta, a invasão da água e formação de lagos salinos. Nesta fase, há intensa atividade vulcânica.</p>

      <p>No segundo estágio, a fragmentação é total e há formação de dois continentes efetivamente separados por um oceano. A atividade vulcânica persiste pela ascensão do magma.</p>

      <p>A permanência da atividade do magma define a chegada ao terceiro estágio, denominado formação de oceano. O principal exemplo do limite divergente em seus três estágios está no Oceano Atlântico, que separa Europa, África e América.</p>

      <p>A divisão dos continentes originou-se há 180 milhões de anos a uma velocidade média de 1 centímetro por ano.</p>

      <h3>Movimentos convergentes das placas tectônicas</h3>

      <p>Esta é a definição para o movimento de colisão de uma placa sobre a outra. Existem três tipos de convergência entre as placas tectônicas: continental-continental, oceânica-oceânica e oceânica-continental.</p>

      <p>O movimento convergente entre placas continentais cria uma área chamada de zona de metamorfismo, sendo responsável por dobramentos modernos, terremotos e atividade vulcânica.</p>

      <p>A convergência entre placas oceânicas cria uma zona de subdução, na qual uma placa tende a deslizar sob a outra gerando uma fossa.</p>

      <p>Nesses locais encontram-se as maiores profundidades dos oceanos, como a Fossa das Marianas, com quase 11 quilômetros de profundidade.</p>

      <p>Já convergência oceânica-continental ocorre quando esses dois tipos de placas se chocam. A placa oceânica, mais densa, mergulha sob a placa continental criando uma zona de subdução, enquanto a placa continental se eleva, formando grandes cadeias de montanhas.</p>

      <p>Por exemplo, a Cordilheira dos Andes foi formada a partir do movimento convergente entre a Placa de Nazca (oceânica) e a Placa Sul-americana (continental). Esse tipo de formação geológica é conhecida como dobramentos modernos.</p>

      <h3>Movimentos conservativos das placas tectônicas</h3>

      <p>O movimento conservativo ocorre em áreas de falhas, onde as placas deslizam uma em relação à outra, vertical ou horizontalmente e de forma paralela, sem divergência ou convergência.</p>

      <p>A fricção causada por esses limites gera a chamada zona de terremotos. Nesses locais, ocorrem os chamados terremotos de focos rasos, que possuem uma grande intensidade.</p>

