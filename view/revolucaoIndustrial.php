            <h1><u>Revolução Industrial</u></h1>

            <p>A <strong>Revolução Industrial</strong> foi um processo de grandes transformações tecnológicas, sociais e econômicas que começou na Inglaterra no século XVIII.</p>

            <p>O modo de produção industrial se espalhou por grande parte do hemisfério Norte durante todo o século XIX e início do século XX. Produzir mercadorias ficou mais barato e acessível, porém trouxe a desorganização da vida rural e estragos ao meio ambiente.</p>

            <p>O advento da produção em larga escala mecanizada deu início às transformações dos países da Europa e da América do Norte. Estas nações se transformaram em predominantemente industriais e suas populações se concentraram cada vez mais nas cidades.</p>

            <p>Por isso, a revolução industrial é caracterizada como o processo que levou à substituição das ferramentas pelas máquinas, da energia humana pela energia motriz e do modo de produção doméstico (ou artesanal) pelo sistema fabril.</p>

            <img src="https://static.todamateria.com.br/upload/im/ag/image5-cke.jpg" alt="">
            <h5>O motor a vapor foi essencial para aumentar a produção das máquinas e a velocidade dos transportes</h5>

            <h2>Causas da Revolução Industrial</h2>

            <p>A expansão do comércio internacional dos séculos XVI e XVII trouxe um extraordinário aumento da riqueza para a burguesia. Isto permitiu a acumulação de capital capaz de financiar o progresso técnico e o alto custo da instalação nas indústrias.</p>

            <p>A burguesia europeia, fortalecida e enriquecida, passou a investir na elaboração de projetos para aperfeiçoamento das técnicas de produção e na criação de máquinas para a indústria.</p>

            Logo verificou-se que se obtinha maior produtividade e se aumentavam os lucros quando se empregavam máquinas em grande escala.</p>

            <h2>Consequências da Revolução Industrial</h2>

            <p>O longo caminho de descobertas e invenções foi uma forma de distanciar os países entre si, no que diz respeito ao poder econômico e político.</p>

            <p>Afinal, nem todos se industrializaram ao mesmo tempo, permanecendo na condição de fornecedores de matérias-primas e produtos agrícolas para os países industrializados.</p>

            <p>Essas diferenças marcam até hoje as nações do mundo que são divididas entre <strong>países desenvolvidos e em desenvolvimento</strong>. Uma das maneiras de medir se um país é avançado é avaliar o quanto ele é industrializado.</p>

            <h2>Fases da Revolução Industrial</h2>

            <p>Foi na Inglaterra que o fenômeno da industrialização começou e por isso a Revolução Industrial Inglesa foi pioneira. Vários fatores explicam as razões desta primazia.</p>

            <p>A Inglaterra, possuía capital, estabilidade política e equipamentos necessários para tomar a dianteira do avanço da Indústria.</p>

            <p>Desde o fim da Idade Média, parte significativa da população se dirigia às cidades devido aos cercamentos (enclousers) do campo. Sem terra, os camponeses acabavam entrando nas fábricas que surgiam.</p>

            <p>Também tinha colônias na África e na Ásia que garantiam fornecimento de matéria-prima com mão de obra barata.</p>

            <h3>Primeira Revolução Industrial</h3>

            <p>A Primeira Revolução Industrial ocorreu em <strong>meados do século XVIII e do século XIX</strong>. Sua principal característica foi o <strong>surgimento da mecanização</strong> que operou significativas transformações em quase todos os setores da vida humana.</p>

            <p>Na estrutura socioeconômica, fez-se a separação definitiva entre o capital, representado pelos donos dos meios de produção, e o trabalho, representado pelos assalariados. Isto eliminou a antiga organização dos grêmios ou guildas que era o modo de produção utilizado pelos artesãos.</p>

            <p>Desta maneira, surgem as primeiras fábricas que abrigam num mesmo espaço muitos operários. Cada um deverá operar uma máquina específica para realizar sua tarefa.</p>

            <img src="https://static.todamateria.com.br/upload/im/ag/image6.jpg?auto_optimize=low" alt="">
            <h5>Mulheres e crianças eram usadas como mão de obra barata nas fábricas inglesas</h5>

            <p>Devido à baixa remuneração, condições de trabalho e de vida sub-humanas, os operários se organizam. Desta forma, associaram-se em organizações trabalhistas e sindicatos para reivindicar melhores jornadas menores e aumento de salários.</p>

            <p>A mecanização se estendeu do setor têxtil para a metalurgia, transportes, agricultura, pecuária e todos os outros setores da economia, inclusive o cultural.</p>

            <p>A Revolução Industrial estabeleceu a definitiva supremacia burguesa na ordem econômica. Ao mesmo tempo, acelerou o êxodo rural, o crescimento urbano e a formação da classe operária. Era o início de uma nova época, onde a política, a ideologia e a cultura gravitavam em dois polos: a burguesia industrial e financeira e o proletariado.</p>

            <p>As fábricas empregavam grande número de trabalhadores. Todas essas inovações influenciaram a aceleração do contato entre culturas e a própria reorganização do espaço e do capitalismo.</p>

            <p>Nessa fase, o Estado passou a participar cada vez mais da economia, regulando crises econômicas e o mercado e criando uma infraestrutura em setores que exigiam muitos investimentos.</p>

            <h3>Segunda Revolução Industrial</h3>

            <p>A partir do <strong>final do século XIX</strong>, o capitalismo se tornava cada vez menos competitivo e mais monopolista. Apenas poucas empresas ou países dominavam a produção e o comércio. Era a <strong>fase do capitalismo financeiro ou monopolista</strong>, característica marcante da Segunda Revolução Industrial.</p>

            <p>Nesta época, o Império Alemão surge como a grande potência industrial. Com a abundância do minério de ferro e uma cultura militar, os alemães, capitaneados pela Prússia, fazem reformas políticas e econômicas que vão unificar o país e dotá-lo de uma indústria poderosa.</p>

            <p>Desde então, se estabeleciam as bases do progresso tecnológico e científico, visando a inovação e o constante aperfeiçoamento dos produtos e técnicas, para melhorar o desempenho industrial.</p>

            <h3>Terceira Revolução Industrial</h3>

            <p>O ponto culminante do desenvolvimento industrial, em termos de tecnologia, teve início em <strong>meados do século XX</strong>, por volta de 1950, com o desenvolvimento da eletrônica. Esta permitiu o <strong>desenvolvimento da informática e a automação das indústrias</strong>.</p>
            <p>Deste modo, as indústrias foram dispensando a mão de obra humana e passaram a depender cada vez mais das máquinas para fabricar seus produtos. O trabalhador intervinha como supervisor ou em apenas algumas etapas da produção.</p>

            <p>Essa fase de novas descobertas caracterizou a Terceira Revolução Industrial ou revolução informática e tecnológica.</p>

            <h2>Revolução Industrial no Brasil</h2>

            Enquanto na Inglaterra, no século XVIII, acontecia a Revolução Industrial, o Brasil, ainda colônia portuguesa, estava longe do processo de industrialização.</p>

            <p>Após a independência houve apenas iniciativas isoladas em instalar indústrias no Brasil. No começo do século XX, fábricas têxteis, principalmente, surgiam em São Paulo e no Rio de Janeiro.</p>

            <p> A industrialização no Brasil, contudo, <strong>só começou verdadeiramente em 1930</strong>, cem anos após a Revolução Industrial Inglesa.</p>

            <p>Durante o governo de Getúlio Vargas, a centralização do poder no Estado Novo criou condições para que se iniciasse o trabalho de coordenação e planejamento econômico. Vargas pôs ênfase na industrialização por substituição de importações.</p>

            <p>A Segunda Guerra Mundial (1939-1945) trouxe uma desaceleração para a industrialização no Brasil, uma vez que interrompeu as importações de máquinas e equipamentos.</p>

            <p>Mesmo assim, o Brasil, através de acordos com os Estados Unidos, consegue fundar a Companhia Siderúrgica Nacional (1941) e a Usiminas (1942).</p>

            <p>Após o conflito, o Estado retornaria suas atividades de investidor e impulsionaria a criação de indústrias como a Petrobras (1953).</p>

