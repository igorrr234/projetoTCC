            <h1><u>Sócrates</u></h1>

            <p> <strong>Sócrates</strong> (470 a.C.-399 a.C.) foi um <strong>filósofo grego</strong>, mesmo não sendo o primeiro filósofo da história, é reconhecido como o "pai da filosofia" por representar o grande marco da filosofia ocidental.</p>

            <h2>Biografia de Sócrates</h2>
            <img src="https://static.todamateria.com.br/upload/so/cr/socratesazul-cke.jpg" alt="" style="width: 60%;">

            <p>Sócrates (c. 469-399 a.C.) nasceu em Atenas, que em meados do século V a.C. tornou-se a metrópole da cultura grega.</p>

            <p>Da sua infância pouco se sabe para além de sua origem pobre. Ele era filho de um escultor, Sofronisco, e uma parteira, Fenarete, da qual Sócrates pegaria a ideia do parto para sua forma de fazer filosofia.</p>

            <p>Homem feito, chamava atenção não só pela sua inteligência mas também pela estranheza de sua figura e seus hábitos. Corpulento, olhos saltados, vestes rotas e pés descalços, era considerado o homem mais feio de Atenas.</p>

            <p>Costumava ficar horas mergulhado em seus pensamentos. Quando não estava meditando solitário, conversava com seus discípulos, procurando ajudá-los na busca da verdade.</p>

            <p>Nessa época, teve início a segunda fase da filosofia grega, conhecida como socrática ou antropológica, onde Sócrates foi o principal filósofo desse período da <strong>filosofia antiga</strong>. Nessa fase, os filósofos passaram a se preocupar com os problemas relacionados ao indivíduo e a organização da humanidade.

            <p>Passaram a perguntar: O que é verdade? O que é o bem? O que é a justiça?, uma vez que na primeira fase da filosofia grega a preocupação era com a origem do mundo, fase que ficou conhecida como <strong>periodo pré-socrático</strong> da filosofia.</p>

            <h2>Principais Ideias de Sócrates</h2>

            <img src="https://static.todamateria.com.br/upload/or/ac/oraculodedelfos-cke.jpg?auto_optimize=low" alt="" style="width: 60%;">
            <h5>Ruínas do Oráculo de Delfos, templo do deus Apolo. Em sua entrada, lia-se "conhece-te a ti mesmo"</h5>

            <p>Para Sócrates, existiam verdades universais, válidas para toda a humanidade em qualquer espaço e tempo. Para encontrá-las, era necessário refletir sobre elas. Essa percepção da verdade como alcançável é um fator de diferenciação entre Sócrates e os <strong>sofistas</strong>.</p>

            <p>O princípio da filosofia de Sócrates estava na frase <strong>"Conhece-te a ti mesmo"</strong>, um oráculo universal dado pelo deus Apolo na <strong>mitologia grega</strong>. Antes de lançar-se em busca de qualquer verdade, o homem precisa se auto analisar e reconhecer sua própria ignorância.</p>

            <p>O próprio Sócrates ao consultar o Oráculo de Delfos recebeu a mensagem de que ele era o mais sábio entre os gregos.</p>

            <p>Sócrates percebeu que ele era sábio porque, dentre os sábios, era o único que julgava não saber e buscava o verdadeiro conhecimento. Da afirmação de sua própria ignorância faz surgir a célebre frase:</p>

            <p><strong>"Só sei que nada sei."</strong></p>
            <br>
            <p>A partir desta ideia, é desenvolvido o Método Socrático. O filósofo inicia uma discussão e conduz seu interlocutor ao reconhecimento de sua própria ignorância através do diálogo: é a primeira fase de seu método, chamada de ironia ou refutação.</p>

            <p>Na segunda fase, a "maiêutica" (técnica de trazer à luz), Sócrates solicita vários exemplos particulares do que está sendo discutido.</p>

            <p>Por exemplo, ao ser questionado sobre a coragem, desenvolve um diálogo com um general muito respeitado por sua atuação em guerras. O general (Laques) lhe dá exemplos de atos corajosos. Não satisfeito, Sócrates analisa esses casos com a finalidade de descobrir o que é comum a todos eles.</p>

            <p>Esse algo comum poderia representar o conceito de coragem, a essência dos atos heroicos, que existirá em qualquer ato corajoso, independente das circunstâncias que o cercarem.</p>

            <p>A "técnica de trazer à luz" pressupõe uma crença de Sócrates, segundo a qual a verdade está no próprio homem, mas ele não pode atingi-la porque não só está envolto em falsas ideias, em preconceitos, como está desprovido de métodos adequados.</p>

            <p>Derrubado esses obstáculos, chega-se ao conhecimento verdadeiro, que Sócrates identifica como virtude, contraposta ao vício, o qual se deve unicamente à ignorância.</p>

            <p><strong>"Ninguém faz o mal voluntariamente."</strong></p>

            <h2>A Morte de Sócrates</h2>
            <img src="https://static.todamateria.com.br/upload/80/0p/800pxdavidthedeathofsocrates-0-cke.jpg?auto_optimize=low" alt="" style="width: 60%;">
            <h5>Cercado de amigos e seguidores em profunda tristeza, Sócrates recebe o cálice com cicuta após condenação à morte</h5>

            <p>Sócrates era uma figura célebre de Atenas. Por onde ia, carregava consigo uma quantidade imensa de seguidores e discípulos, sobretudo jovens.</p>
            <p>Em seus encontros com figuras respeitadas da <strong>polis grega</strong>, por conta de seu método, acabava expondo e irritando seus interlocutores.</p>

            <p>Esse comportamento deu a Sócrates inimigos entre as figuras mais poderosas de Atenas. Em pouco tempo, o filósofo foi acusado de corromper a juventude e atentar contra os deuses gregos.</p>

            <p>Seu julgamento foi realizado em duas partes. Na primeira, a votação sobre sua culpa ou inocência teve uma margem apertada a favor de sua condenação (280 a 220).</p>

            <p>Posteriormente, Sócrates propõe como pena alternativa o pagamento de uma multa. Essa pena é amplamente recusada e a condenação é a favor da pena capital (360 a 141).</p>

            <p>Sócrates aceita o julgamento e despede-se com a frase:</p>

            <p><strong>"É a hora de irmos: eu para a morte, vós para as vossas vidas; quem terá a melhor sorte? Só os deuses sabem."</strong></p>

            <h2>O legado de Sócrates</h2>

            <p>Sócrates não deixou obra escrita, achava mais eficiente o intercâmbio de ideias, mediante perguntas e respostas entre duas pessoas e acreditava que a escrita enrijecia o pensamento.</p>

            <p>São quatro as fontes básicas para o conhecimento de Sócrates: o filósofo <strong>Platão</strong>, seu discípulo, em cujos Diálogos o mestre figura sempre como personagem central.</p>

            <p>A segunda fonte é o historiador Xenofonte, amigo e frequentador assíduo das reuniões que Sócrates participava.</p>

            <p>O dramaturgo Aristófanes cita Sócrates como personagem em algumas de suas comédias, mas sempre o ridiculariza.</p>

            <p>A última fonte é <strong>Aristóteles</strong>, discípulo de Platão, e que nasceu 15 anos após a morte de Sócrates. Essas fontes nem sempre são coerentes entre si.</p>

