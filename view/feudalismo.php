      <h1><u>Feudalismo</u></h1>


      <p>O <strong>Feudalismo</strong> foi uma organização econômica, política e social baseada na posse da terra - o feudo - que predominou na Europa Ocidental durante a Baixa Idade Média. Ele surgiu do encontro entre os costumes romanos e germânicos, através das invasões bárbaras no então Império Romano do Ocidente.</p>

      <p>As terras e títulos de nobreza eram doadas pelo rei como recompensa aos líderes por terem participado de batalhas.</p>

      <p>O feudo era uma grande propriedade rural que abrigava o castelo fortificado, as aldeias, as terras para cultivo, os pastos e os bosques.</p>

      <p>Teve origem no Império Carolíngio, quando o rei precisava de aliados defender suas extensas fronteiras. A partir do século IX, quando o este império se desintegra, o que sobrou foram várias regiões independentes entre si, governadas por um nobre.</p>

      <img src="https://static.todamateria.com.br/upload/fe/ud/feudalismoservos-cke.jpg" alt="" style="width: 65%;">

      <h2>Características do Feudalismo</h2>

      <h3>Sociedade Feudal</h3>

      <p>A sociedade, no feudalismo, era denominada estamental porque era composta por camadas sociais que se diferenciavam pelos privilégios que possuíam.</p>

      <p>Existiam <strong>três estamentos sociais– nobreza, clero e servos</strong>.</p>

      <p>Quase não existia mobilidade social e passar de um estamento social para outro era praticamente impossível.</p>

      <h3>Nobreza</h3>


      <p>A <strong>nobreza</strong> era proprietária de terras e seus integrantes eram chamados <strong>senhores feudais</strong>. Estes aplicavam as leis, concediam privilégios, comercializavam com os vizinhos, administravam a justiça, declaravam as guerras e faziam a paz.</p>

      <p>No <strong>topo da nobreza estava o rei</strong>, que concentrava pouco poder político, pois este era dividido entre o monarca e os senhores feudais. No entanto, o monarca tinha prestígio junto aos outros senhores feudais.</p>

      <h3>Clero</h3>

      <p>A Igreja se tornou a mais poderosa instituição feudal, pois foi proprietária de vastas extensões de terra, além de determinar diretamente as relações sociais.</p>


      <p>Segundo ela, <strong>cada membro da sociedade tinha uma função a cumprir em sua passagem pela terra</strong>. A função do nobre era proteger militarmente a sociedade, a do clero, rezar e a do servo, trabalhar.</p>

      <p>Além disso, os <strong>mosteiros medievais</strong> foram responsáveis pela conservação de manuscritos sobre literatura, filosofia e ciência, apoiavam os viajantes e acolhiam os doentes.</p>

      <h3>Servos</h3>

      <p>O trabalho estava fundamentado na servidão, com os <strong>camponeses presos à terra</strong> e subordinados a uma série de obrigações, desde impostos e serviços.</p>

      <p>Por outro lado, os senhores feudais deveriam protegê-los em caso de ataques.</p>

      <p>Além dos servos, havia outros trabalhadores como:</p>
      <br>
      <p><strong>• Vilões</strong>: homens livres que moravam na vila, mas podiam prestar serviços ao senhor feudal e tinham permissão para trocar de propriedade;</p>
      <p><strong>• Ministeriais</strong>: ocupavam a administração da propriedade feudal e podiam ascender socialmente, chegando à condição membros da pequena nobreza.</p>
      <p><strong>• Escravizados</strong>: em geral, empregados no serviço doméstico. Nesta época era comum os cristãos escravizarem muçulmanos e vice-versa.</p>
      <p>As condições de vida nos domínios feudais eram rudes e mesmo a nobreza não vivia luxuosamente.</p>

      <p>A vida dos servos era miserável em todos os sentidos. Os servos e mesmo os senhores feudais não sabiam ler nem escrever. O clero era o único estamento social que tinha acesso ao estudo.</p>

      <h3>Economia feudal</h3>

      <p>A economia no feudalismo se caraterizava pela <strong>produção autossuficiente</strong>, pois se destinava ao consumo local, dentro do próprio feudo, e não ao comércio em larga escala.</p>

      <p>Na época de boas colheitas, os excedentes eram trocados em feudos vizinhos ou nas feiras que ocorriam nas cidades. Muitas vezes o comércio ocorria através do <strong>intercâmbio de gêneros e não moedas</strong>; porém, estas existiam e eram emitidas por cada feudo.</p>

      <h3>Política feudal</h3>


      <p>O <strong>poder político</strong> no feudo era exercido pelo senhor feudal, que possuía exército, cobrava impostos e distribuía a justiça. No entanto, sua obrigação era proteger os servos e, para isso, construía um castelo fortificado, em torno do qual se desenvolvia a comunidade.</p>

      <p>Quando um senhor feudal necessitava apoio para a guerra, fazia alianças com nobres menos poderosos. Através de um juramento de fidelidade - chamado "homenagem" - <strong>o senhor feudal com mais recursos se tornava suserano e o outro, vassalo</strong>. Em retribuição, este recebia terras ou as rendas de algum pedágio ou do moinho, por exemplo. Contudo, por sua parte, o vassalo deveria defender o suserano e acompanhá-lo em caso de conflito.</p>

      <p>Importante lembrar que os membros do clero poderiam ser senhores feudais. Os mosteiros, além do edifício religioso, possuíam grandes extensões de terra para seu sustento.</p>


      <h2>Como ocorriam as concessões de terra?</h2>


      <p><strong>Um feudo podia ser obtido</strong> das seguintes maneiras:</p>
      <br>
      <p><strong>• Concessão do rei ou senhor feudal</strong>: para compensar os serviços de um nobre ou de um cavaleiro destacado e assim conseguir a vassalagem;</p>
      <p><strong>• Casamentos</strong>: assegurava a fidelidade dos senhores feudais e garantia que a terra continuaria na mesma família;</p>
      <p><strong>• Guerras</strong>: quando os laços de vassalagem eram rompidos, uma família não tinha herdeiros ou mesmo por desejar expandir suas terras, era comum fazer guerras para obter mais territórios.</p>

      <h2>Crise do feudalismo</h2>


      <p>O <strong>feudalismo</strong> sofreu grandes transformações a partir do <strong>século XIII</strong>.</p>

      <p>Nesta época, o desenvolvimento do comércio e das cidades ampliaram as fontes de renda. À medida que o poder foi se concentrando nas mãos de um único rei, as vilas e cidades conquistaram mais autonomia. O soberano, então, lhes concediam diversas imunidades, como isenção fiscal e jurídica, o que diminuía a importância do feudo.</p>

      <p>Com isso, o dinheiro começou a adquirir mais valor do que a terra e as relações de produção passaram a ser baseadas no trabalho livre e assalariado. <strong>Houve também o surgimento de novas camadas sociais, como a burguesia</strong>.</p>

      <p>O crescimento populacional foi um dos fatores responsáveis pelas mudanças no sistema feudal de produção. À medida que crescia a população, aumentava a necessidade de ampliar a área de plantio e desenvolver novas técnicas agrícolas.</p>

      <p>Para aumentar o terreno de cultivo, os senhores feudais passaram a cercar as terras comunais, ou seja, as áreas que eram utilizadas por todos os servos. Alguns deles arrendaram as terras, enquanto outros passaram a vender a liberdade aos servos ou a expulsá-los do feudo, colocando em seu lugar trabalhadores assalariados.</p>

      <p>Isto acabou gerando revolta entre os camponeses que responderam de forma violenta. Outro fator de êxodo rural foi o próprio crescimento das cidades que se tornava mais atrativa para muitos servos.</p>

      <p>O processo de mudança do sistema feudal pelo sistema capitalista foi lento e gradual, e ocorreu com o renascimento comercial, a centralização monárquica e o aparecimento da burguesia.</p>


