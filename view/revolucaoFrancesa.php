            <h1><u>Revolução Francesa</u></h1>

            <p>A <strong>Revolução Francesa</strong>, iniciada no dia 17 de junho de 1789, foi um movimento impulsionado pela burguesia e contou com a participação dos camponeses e das classes urbanas que viviam na miséria.</p>

            <p>Em 14 de julho de 1789, os parisienses tomaram a prisão da Bastilha desencadeando profundas mudanças no governo francês.</p>

            <img src="https://static.todamateria.com.br/upload/re/vo/revolucao-francesa-og.jpg" alt="" width="70%">
            <h2>Contexto histórico</h2>


            <p>No final do século XVIII, a França era um país agrário, com a produção estruturada no modelo feudal. Isso significava que existiam impostos e licenças que só eram válidos para determinadas regiões. O poder político estava concentrado no rei e num pequeno número de auxiliares.</p>

            <p>Por isso, para a burguesia e parte da nobreza era preciso acabar com o poder absoluto do rei Luís XVI.</p>

            <p>Enquanto isso, do outro lado do Canal da Mancha, a Inglaterra, sua rival, desenvolvia o processo de Revolução Industrial.</p>

            <h2>Fases da Revolução Francesa</h2>

            <p>Para fins de estudo, a Revolução Francesa é dividida em três fases:</p>
            <br>
            <p>• Monarquia Constitucional (1789-1792);</p>
            <p>• Convenção Nacional (1792-1795);</p>
            <p>• Diretório (1795-1799).</p>



            <h2>Causas da Revolução Francesa</h2>


            <p>A burguesia francesa, preocupada em desenvolver a indústria no país, queria acabar com as barreiras que restringiam a liberdade de comércio internacional. Desta forma, era preciso que se adotasse na França, segundo a burguesia, o liberalismo econômico.</p>

            <p>A burguesia exigia também a garantia de seus direitos políticos, pois era ela quem sustentava o Estado, posto que o clero e a nobreza estavam livres de pagar impostos.</p>

            <p>Apesar de ser a classe social economicamente dominante, sua posição política e jurídica era limitada.</p>


            <h3>Iluminismo</h3>


            <p>O <a href="iluminismo.php">iluminismo</a> se propagou entre os burgueses e propulsionou o início da Revolução Francesa.</p>

            <p>Este movimento intelectual fazia duras críticas às práticas econômicas mercantilistas, ao absolutismo, e aos direitos concedidos ao clero e à nobreza.</p>

            <p>Seus autores mais conhecidos foram Voltaire, Montesquieu, Rousseau, Diderot e Adam Smith.</p>

            <h3>Crise econômica e política</h3>

            <p>A crítica situação econômica, às vésperas da revolução de 1789, exigia reformas, mas gerava uma crise política. Esta se agravou quando os ministros sugeriram que a nobreza e o clero deveriam pagar impostos.</p>

            <p>Pressionado pela situação, o rei Luís XVI convoca os Estados Gerais, uma assembleia formada pelos três estamentos da sociedade francesa:</p>
            <br>
            <p><strong>• Primeiro Estado</strong> - composto pelo clero;</p>
            <p><strong>• Segundo Estado</strong> - formado pela nobreza;</p>
            <p><strong>• Terceiro Estado</strong> - composto por todos aqueles que não pertenciam ao Primeiro nem ao Segundo Estado, no qual se destacava a burguesia.</p>
            <br>
            <p>O Terceiro Estado, mais numeroso, pressionava para que as votações das leis fossem individuais e não por Estado. Somente assim, o Terceiro Estado poderia passar normas que os favorecessem.</p>

            <p>No entanto, o Primeiro e o Segundo Estado recusaram esta proposta e as votações continuaram a ser realizadas por Estado.</p>

            <p>Desta forma, reunidos no Palácio de Versalhes, o Terceiro Estado e parte do Primeiro Estado (baixo clero) se separam da Assembleia. Em seguida, declaram-se os legítimos representantes da nação, formando a Assembleia Nacional Constituinte e jurando permanecer reunidos até que ficasse pronta a Constituição.</p>

            <img src="https://static.todamateria.com.br/upload/re/vo/revolucaofrancesa-cke.jpg" alt="">
            <h5>O juramento na Sala de Jogo de Paume, de Jean-Louis David, ilustra a união entre parte do Primeiro Estado e o Terceiro.</h5>

            <h2>Monarquia constitucional (1789-1792)</h2>

            <p>No dia 26 de agosto de 1789 foi aprovada pela Assembleia a Declaração dos Direitos do Homem e do Cidadão.</p>

            <p>Esta Declaração assegurava os princípios da liberdade, da igualdade, da fraternidade (“Liberté, égalité, fraternité” - lema da Revolução), além do direito à propriedade.</p>

            <p>A recusa do rei Luís XVI em aprovar a Declaração provocam novas manifestações populares. Os bens do clero foram confiscados e muitos padres e nobres fugiram para outros países. A instabilidade na França era grande.</p>

            <p>A Constituição ficou pronta em setembro de 1791. Dentre os artigos podemos destacar:</p>
            <br>
            <p>• o governo foi transformado em monarquia constitucional;</p>
            <p>• o poder executivo caberia ao rei, limitado pelo legislativo, constituído pela Assembleia;</p>
            <p>• os deputados teriam mandato de dois anos;</p>
            <p>• instituído o voto censitário (só seria eleitor quem tivesse uma renda mínima);</p>
            <p>•suprimiram-se os privilégios e as antigas ordens sociais;</p>
            <p>• confirmaram-se a abolição da servidão e a nacionalização dos bens eclesiásticos;</p>
            <p>• manteve-se a escravidão nas colônias.</p>

            <h2>Convenção Nacional (1792-1795)</h2>

            <p>A Assembleia Legislativa foi substituída, através do sufrágio universal masculino, pela Convenção Nacional, que aboliu monarquia e implantou a República. Os jacobinos eram a maioria neste novo parlamento.</p>

            <p>O rei Luís XVI foi julgado e sentenciado culpado por traição, sendo condenado à morte por guilhotina e executado em janeiro de 1793. Meses depois, a rainha Maria Antonieta teria o mesmo destino.</p>

            <p>Internamente, as opiniões divergentes de como deveriam ser conduzida a revolução, começavam a provocar divisão entre os próprios revolucionários. Existiam basicamente dois grupos:</p>
            <br>
            <p>Os <strong>girondinos</strong> - representantes da alta burguesia, defendiam posições moderadas e a monarquia constitucional.</p>

            <p>Por sua parte, os <strong>jacobinos</strong> - representantes da média e da pequena burguesia, constituía o partido mais radical, sob a liderança de Maximilien Robespierre. Queriam a instalação de uma república e um governo popular.</p>


            <h3>O Terror (1793-1794)</h3>

            <p>Dentro do período da Convenção Nacional existe um ano extremamente violento, onde as pessoas suspeitas de serem contrarrevolucionárias eram condenadas à guilhotina. Este período ficou conhecido como "terror".</p>

            <p>Isto foi possível graças a aprovação da <strong>Lei dos Suspeitos</strong> que autorizava a prisão e morte dos considerados antirrevolucionários. Nessa mesma altura, as igrejas eram encerradas e os religiosos obrigados a deixar seus conventos. Aqueles que recusavam a jurar a Constituição Civil do Clero eram executados. Além da guilhotina, os suspeitos eram afogados no rio Loire.</p>

            <p>A ditadura jacobina introduziu novidades na Constituição como:</p>
            <br>
            <p>• voto universal e não censitário;</p>
            <p>• fim da escravidão na colônias;</p>
            <p>• congelamento de preços de produtos básicos como o trigo;</p>
            <p>• instituição do Tribunal Revolucionário para julgar os inimigos da Revolução.</p>
            <br>
            <p>As execuções tornaram-se um espetáculo popular, pois aconteciam diversas vezes ao dia num ato público. Para os ditadores eram uma forma justa de acabar com os inimigos, porém essa atitude causava medo na população que se voltou contra Robespierre e o acusou de tirania.</p>

            <p>Nessa sequência, após ser detido, Robespierre foi executado e este fato ficou conhecido como “Golpe do 9 Termidor”, em 1794.</p>


            <img src="https://static.todamateria.com.br/upload/te/rr/terror-cke.jpg?auto_optimize=low" alt="" style="width: 65%;">


            <h2>Diretório (1794-1799)</h2>


            <p>A fase do Diretório dura cinco anos e se caracteriza pela ascensão da alta burguesia, os girondinos, ao poder. Recebe este nome, pois eram cinco diretores que governavam a França.</p>

            <p>Inimigos dos jacobinos, seu primeiro ato é revogar todas as medidas que eles haviam feito durante sua legislação. No entanto, a situação era delicada. Os girondinos atraíram a antipatia da população ao anular o congelamento de preços.</p>

            <p>Vários países, como a Inglaterra e o Império Austríaco, ameaçavam invadir a França a fim de conter os ideais revolucionários. Por fim, a própria nobreza e a família real no exílio buscavam organizar-se para restaurar o trono.</p>

            <p>Diante desta situação, o Diretório recorre ao Exército, na figura do jovem general Napoleão Bonaparte para conter os ânimos dos inimigos.</p>

            <p>Desta maneira, Bonaparte dá um golpe - o 18 Brumário - onde instaura o Consulado, um governo mais centralizado que traria paz ao país por alguns anos.</p>

            <h2>Consequências da Revolução Francesa</h2>

            <img src="https://static.todamateria.com.br/upload/na/po/napoleaobonaparte-cke.jpg?auto_optimize=low" alt="" style="width: 70%;">
            <h5>Napoleão Bonaparte espalhou os ideais da Revolução Francesa através de guerras pela Europa</h5>

            <p>Em dez anos, de 1789 a 1799, a França passou por profundas modificações políticas, sociais e econômicas.</p>

            <p>A aristocracia do Antigo Regime perdeu seus privilégios, libertando os camponeses dos laços que os prendiam aos nobres e ao clero. Desapareceram as amarras feudais que limitavam as atividades da burguesia e criou-se um mercado de dimensão nacional.</p>

            <p>A Revolução Francesa foi a alavanca que levou a França do estágio feudal para o capitalista e mostrou que a população era capaz de condenar um rei.</p>

            <p>Igualmente, instalou a separação de poderes e a Constituição, uma herança deixada para várias nações do mundo.</p>

            <p>Em 1799, a alta burguesia aliou-se ao general Napoleão Bonaparte, que foi convidado a fazer parte do governo. Sua missão era recuperar a ordem e a estabilidade do país, proteger a riqueza da burguesia e salvá-los das manifestações populares.</p>

            <p>Por volta de 1803 têm início as Guerras Napoleônicas, conflitos revolucionários imbuídos dos ideais da Revolução Francesa que teve como protagonista Napoleão Bonaparte.</p>




