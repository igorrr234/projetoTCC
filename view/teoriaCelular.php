            <h1><u>Teoria Celular</u></h1>

            <p>A <strong>Teoria Celular</strong> foi criada por Mathias Scheiden e Theodor Schwann e afirma que todos os seres vivos são formados por células.</p>

            <p>O estabelecimento da Teoria Celular foi possível graças ao desenvolvimento da microscopia.</p>

            <p>Atualmente, é uma das generalizações mais importantes da Biologia.</p>


            <h2>Histórico da Teoria Celular</h2>

            <p>Em 1665, Robert Hooke analisou fatias de cortiça ao microscópio e observou que eram formadas por cavidades microscópicas, as quais denominou de células.</p>

            <p>A palavra célula é originária do latim, cellula, diminutivo de cella, pequeno compartimento.</p>

            <p>O microscopista holandês Antoni van Leeuwenhoek foi o primeiro a registrar células livres.</p>

            <p>Em 1674, relatou a descoberta do protozoário. Em 1677, do espermatozoide humano e em 1683, da bactéria.</p>

            <p>Com o aprimoramento da microscopia, Roberto Brown descobriu o núcleo da célula, em 1833.</p>

            <p>Em 1838, Mathias Schleiden formulou o princípio de que todos os vegetais são constituídos de células.</p>

            <p>Em 1839, esse princípio foi estendido para os animais por Theodor Schwann.</p>

            <p>Walther Flemming, em 1882, observou o aparecimento de filamentos no núcleo da célula em divisão.</p>

            <p>Esses estudos e descobertas foram fundamentais para que a Teoria Celular fosse estabelecida.</p>

            <h2>Postulados da Teoria Celular</h2>



            <p>A versão moderna da Teoria Celular tem como fundamentos:</p>

            <p><strong>• Todos os seres vivos são constituídos por células;</strong></p>
            <p><strong>• As atividades essenciais que caracterizam a vida ocorrem no interior das células;</strong></p>
            <p><strong>• Novas células se formam pela divisão de células preexistentes através da divisão celular;</strong></p>
            <p><strong>• A célula é a menor unidade da vida.</strong></p>

            <h2>Os vírus e a Teoria Celular</h2>


            <p>Os vírus não apresentam células em sua constituição, portanto são acelulares.</p>

            <p>Os vírus são parasitos intracelulares obrigatórios.</p>

            <p>Apesar de não possuírem células, eles dependem de células vivas para realizarem suas atividades vitais.</p>

            <p>Isso comprova que atividades essenciais à vida só ocorrem no interior de células vidas, conforme postulado pela Teoria Celular.</p>




