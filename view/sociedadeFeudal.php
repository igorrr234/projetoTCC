    <p>A Sociedade Feudal, um dos pilares fundamentais da Europa medieval, reflete uma estrutura social complexa e altamente hierárquica. Dominante durante a Idade Média, essa sociedade era organizada em camadas distintas, com os senhores feudais no topo, detendo não apenas terras vastas, mas também o poder político sobre a região que governavam. Abaixo deles estavam os vassalos, que, em troca de terras e proteção, juravam fidelidade e comprometiam-se a servi-los militarmente quando necessário. No nível mais baixo da pirâmide social estavam os camponeses, cujas vidas estavam intimamente ligadas à terra que cultivavam.</p>
