      <h1><u>Egito Antigo</u></h1>


      <p>O <strong>Egito Antigo</strong> foi uma das mais importantes civilizações da Antiguidade.</p>

      <p>A vida egípcia estava regulada pelas <strong>cheias do rio Nilo</strong>. Quando as águas voltavam ao leito normal deixavam o solo recoberto com um limo que fertilizava a terra para a agricultura.</p>

      <p>Para melhor aproveitá-lo, os egípcios desenvolveram sistemas de medida e escrita baseada nos <strong>hieroglifos</strong>.</p>

      <p>Quanto à religião eram <strong>politeístas</strong> e no seu panteão cultuavam o deus do Sol, Rá e o deus dos Vivos, Hórus, entre vários outros.</p>

      <img src="https://static.mundoeducacao.uol.com.br/mundoeducacao/2023/09/esfinge-em-frente-a-piramide-de-khafre-estruturas-construidas-no-egito-antigo-uma-das-civilizacoes-mais-antigas-do-mundo.jpg" alt="" style="width: 60%;">


      <h2>História do Antigo Egito</h2>

      <p>O <strong>Egito Antigo</strong> foi formado a partir da mistura de diversos povos, a população era dividida em vários clãs, que se organizavam em comunidades chamadas nomos. Estes funcionavam como se fossem pequenos Estados independentes.</p>

      <p>Por volta de <strong>3500 a.C.</strong>, os nomos se uniram formando dois reinos: o Baixo Egito, ao Norte e o Alto Egito, ao Sul. Posteriormente, em <strong>3200 a.C.</strong>, os dois reinos foram unificados por Menés, rei do alto Egito, que tornou-se o primeiro faraó, criando a primeira dinastia que deu origem ao Estado egípcio.</p>

      <p>Começava um longo período de esplendor da civilização egípcia, também conhecida como a <strong>era dos grandes faraós</strong>.</p>

      <h2>Sociedade egípcia</h2>

      <p>A <strong>antiga sociedade egípcia</strong> estava dividida de maneira rígida e nela praticamente não havia mobilidade social.</p>

      <p>No topo da sociedade encontrava-se o Faraó e sua imensidão de parentes. O faraó era venerado como um verdadeiro deus, pois era considerado como o intermediário entre os seres humanos e as demais divindades. Por isso, era uma monarquia teocrática, ou seja, um governo baseado nas ideias religiosas.</p>

      <p>Abaixo do faraó e de sua família vinham as camadas privilegiadas como sacerdotes, nobres e funcionários. Na base da pirâmide social egípcia estavam os não privilegiados que eram artesãos, camponeses, escravos e soldados.</p>

      <p>Os sacerdotes formavam, junto com os nobres, a corte real. Tanto a nobreza como o sacerdócio eram hereditários compondo a elite militar e latifundiária.</p>

      <p>Os escribas estavam a serviço do Estado para planejar, fiscalizar e controlar a economia. Por isso, sabiam ler e escrever e eram eles que anotavam os feitos do faraó durante o seu reinado. Estes textos seriam colocados nos seus túmulos quando morressem.</p>

      <p>Já o exército era constituído por jovens que eram convocados em tempo de guerra e soldados mercenários estrangeiros contratados pelo Estado.</p>

      <p>Por sua parte, os artesãos eram trabalhadores assalariados que exerciam diferentes ofícios como cortadores de pedra, carpinteiros, joalheiros, etc. Os camponeses formavam a maior parte da população, trabalhavam na agricultura, na criação de animais e deviam pagar altos impostos.</p>



      <img src="https://static.todamateria.com.br/upload/pi/ra/piramidesocialdoantigoegito-cke.jpg" alt="" style="width: 50%;">

      <p>Na <strong>sociedade egípcia</strong>, as mulheres tinham uma posição de prestígio. Podiam exercer qualquer função política, econômica ou social em igualdade com os homens de sua categoria social. Isto significava, inclusive, que poderiam ser faraós, como foi o caso de <strong>Cleópatra</strong>.</p>

      <h2>Civilização egípcia</h2>


      <p>A <strong>civilização egípcia</strong> foi extremamente sofisticada e suas marcas estão entre nós até a atualidade.</p>

      <p>Os egípcios, como todos os povos da Antiguidade, eram ótimos astrônomos e observando a trajetória do sol dividiram o calendário em 365 dias e um dia em 24 horas, que é usado até hoje pela maioria dos povos ocidentais.</p>

      <p>Na medicina, os egípcios escreveram vários tratados sobre remédios para cura das doenças, cirurgias e descrição do funcionamento dos órgãos. Também existiam os médicos especialistas e seus ajudantes, equivalentes aos atuais enfermeiros.</p>

      <p>Na escrita, a sociedade egípcia desenvolveu a escrita pelos hieroglifos. Estes eram figuras de animais, partes do corpo ou objetos do cotidiano que era utilizado para registrar a história, os textos religiosos, a economia do reino, etc.</p>

      <h2>Cultura egípcia</h2>

      <p>A principal arte desenvolvida no Egito Antigo foi a arquitetura. Profundamente marcada pela religiosidade, as construções voltaram-se principalmente para a edificação de grandes templos como os de Karnac, Luxor, Abu-Simbel e as célebres pirâmides de Gizé, que serviam de túmulos aos faraós, entre as quais se destacam Quéops, Quéfren e Miquerinos.</p>

      <p>A pintura egípcia era muito peculiar, pois representava o corpo de frente, mas a cabeça estava sempre de perfil, caso o retratado estivesse de pé. No entanto, se estivesse sentado, tanto o corpo como a cabeça estariam de perfil. Pintavam-se as paredes dos palácios, templos e especialmente, as tumbas destinadas aos faraós.</p>


      <p>A pintura representava cenas familiares e do cotidiano do reino, como procissões, nascimento e morte, mas também, o cultivo e a colheita. Hoje, as pinturas nos permitem reconstruir o dia a dia dos egípcios.</p>

      <p>A escultura egípcia, de grande porte, retratava as esfinges, criaturas fantásticas, deuses e faraós. Merece atenção as obras de pequeno tamanho como os sarcófagos, de pedra ou madeira, nos quais os artífices procuravam reproduzir as feições do morto, para ajudar a alma a encontrar o corpo. Alguns, inclusive, chegavam a incrustar pupilas de cristal nos olhos.</p>

      <h2>Economia egípcia</h2>


      <p>O <strong>rio Nilo</strong> era responsável por mover a economia, pois após as cheias, quando a terra estava fértil, plantavam-se trigo, cevada, frutas, legumes, linho, papiro e algodão. De igual maneira, o Nilo servia para pesca e garantia a unidade política ao antigo Egito, porque era uma via utilizada para comunicar os dois pontos do território.</p>

      <p>Para melhor aproveitar o rendimento do terreno, os egípcios desenvolveram sistemas de medida e contagem. Afinal, os impostos eram pagos conforme o tamanho da área cultivada e era preciso anotar com exatidão as quantidades cobradas.</p>

      <p>A terra pertencia ao faraó e os camponeses eram obrigados a dar parte de seus produtos para o Estado em troca do direito de cultivar o solo. No entanto, a construção de diques, reservatórios e canais de irrigação era tarefa do Estado, que empregava tanto mão de obra livre quanto escrava para fazê-lo.</p>


