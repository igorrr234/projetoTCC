      <h1><u>Grécia Antiga</u></h1>

      <p><strong>Grécia Antiga</strong> é a época da história grega que se estende do <strong>século XX ao século I a.C.</strong></p>

      <p>Quando falamos em Grécia Antiga não estamos nos referindo a um país unificado e sim num conjunto de cidades independentes que compartilhavam a língua, costumes e algumas leis.</p>

      <p>Muitas delas foram até inimigas entre si como foi o caso de Atenas e Esparta.</p>

      <img src="https://static.todamateria.com.br/upload/ma/pa/mapa_gra_cia_antiga_re.jpg" alt="" style="width: 45%;">
      <h5>Mapa da Grécia Antiga</h5>

      <h2>Política</h2>

      <p>No <strong>Período Clássico</strong>, os gregos procuraram cultivar a beleza e a virtude desenvolvendo as artes da música, pintura, arquitetura, escultura, etc.</p>

      <p>Com isso, acreditavam que os cidadãos seriam capazes de contribuir para o bem-comum. Estava lançada, assim, a democracia.</p>

      <p>A <strong>democracia</strong> era o governo exercido pelo povo, ao contrário dos impérios que eram liderados por dirigentes que eram considerados deuses, como foi o caso do Egito dos Faraós.</p>

      <p>A democracia desenvolveu-se principalmente em <strong>Atenas</strong>, onde os homens livres tinham oportunidade de discutir questões políticas em praça pública.</p>

      <h2>Sociedade</h2>

      <p>Cada <strong>polis</strong> tinha sua própria organização social e algumas, como Atenas, admitiam a escravidão, por dívida ou guerras. Por sua vez, Esparta, tinha poucos escravos, mas possuía os servos estatais, que pertenciam ao governo espartano.</p>

      <p>Ambas as cidades tinham uma oligarquia rural que os governava.</p>

      <p>Também em Atenas verificamos a figura dos estrangeiros chamados metecos. Só era cidadão quem nascia na cidade e por isso, os estrangeiros não podiam participar das decisões políticas da polis.</p>

      <h2>Economia</h2>

      <p>A <strong>economia grega</strong> se baseava em produtos artesanais, na agricultura e no comércio.</p>

      <p>Os gregos faziam produtos em couro, metal e tecidos. Estes davam muito trabalho, pois todas as etapas de produção - desde a fiação até o tingimento - eram demoradas.</p>

      <p>Os cultivos estavam dedicados às vinhas, oliveiras e trigo. A isto somavam-se à criação de animais de pequeno porte.</p>

      <p>O comércio ocorria entre as cidades gregas, nas margens do Mediterrâneo e afetava toda sociedade grega. Para realizar as trocas comerciais se usava a moeda "dracma".</p>

      <p>Havia tanto o pequeno comércio do agricultor, que levava sua colheita ao mercado local, quanto o grande comerciante, que possuía barcos que faziam toda rota do Mediterrâneo.</p>

      <h2>Religião</h2>

      <img src="https://static.todamateria.com.br/upload/pa/rt/partenonematenas-cke.jpg?auto_optimize=low" alt="" style="width: 60%;">
      <h5>Templo Partenon, dedicado à deusa Atenas, protetora da cidade de mesmo nome.</h5>


      <p>A <strong>religião da Grécia Antiga</strong> era <strong>politeísta</strong>. Ao receber a influência de vários povos, os gregos foram adotando deuses de outros lugares até constituir o panteão de deuses, ninfas, semideuses e heróis que eram cultuados tanto em casa como publicamente.</p>

      <p>As histórias dos deuses serviam de ensinamento moral à sociedade, e também para justificar atos de guerra e de paz. Os deuses também interferiam na vida cotidiana e, praticamente, havia uma deidade para cada função.</p>

      <p>Se um grego tivesse uma dúvida em relação a qual atitude tomar, poderia consultar o oráculo de Delfos. Ali, uma pitonisa entraria em transe a fim de tomar contato com os deuses e responderia à questão. Como essa era dada de forma enigmática, um sacerdote se encarregaria de interpretá-la ao cliente.</p>

      <h2>Cultura</h2>

      <p>A <strong>cultura grega</strong> está intimamente ligada à religião, pois a literatura, a música e o teatro contavam os feitos dos heróis e de sua relação com os deuses que viviam no Olimpo.</p>

      <p>As peças teatrais eram muito populares e todas as cidades tinham seu espaço cênico (chamado orquestra) onde eram encenadas as tragédias e comédias.</p>

      <p>A música era importante para alegrar banquetes civis e acompanhar atos religiosos. Os principais instrumentos eram a flauta, tambores e harpas. Esta última era utilizada para ajudar os poetas a recitarem suas obras.</p>

      <p>Igualmente, os esportes faziam parte do cotidiano grego. Por isso, para celebrar a aliança entre as diferentes polis, organizavam-se competições nos tempos de paz.</p>

      <p>A primeira delas foi realizada em 776 a.C, na cidade de Olímpia e daí seria conhecida como Jogos Olímpicos, ou simplesmente, Olimpíadas.</p>

      <p>Naquela época, só os homens livres que soubessem falar grego poderiam tomar parte na competição.</p>

