      <h1><u>Tigres Asiáticos</u></h1>

      <p>Os <strong>Tigres Asiáticos</strong> são um <strong>bloco econômico</strong> formado por quatro países da Ásia: Hong Kong, Singapura, Coreia do Sul e Taiwan.</p>

      <p>Tais países começaram a se destacar entre a década de 1960 e de 1970, devido a um rápido e consistente crescimento econômico</p>

      <p>Logo após o fim da Segunda Guerra Mundial, esses países tinham uma economia fraca; eram nações pobres e subdesenvolvidas.</p>

      <p>No entanto, a abertura pós-guerra das fronteiras, a localização geográfica de cada um deles e os investimentos privados internacionais contribuíram para alavancar suas economias.</p>

      <p>O nome do bloco econômico faz analogia às características do tigre; animal selvagem, considerado perspicaz, veloz e agressivo, tal qual o crescimento da economia dos quatro países.</p>

      <img src="https://static.todamateria.com.br/upload/ti/gr/tigresasiaticos-cke.jpg" alt="" style="width:65%">

      <h2>Ascensão da economia dos Tigres Asiáticos</h2>

      <p>O contexto histórico do crescimento da economia dos Tigres Asiáticos está diretamente relacionado com a Guerra Fria.</p>

      <p>Com o objetivo de expandir o capitalismo e frear o socialismo, os Estados Unidos investiu em países do sudeste da Ásia que estavam se reconstruindo após a Segunda Guerra Mundial.</p>

      <p>Para isso, impôs uma condição: tais países deveriam cultivar uma relação diplomática com os EUA (evitando, assim, o avanço do socialismo) e consumir produtos americanos.</p>

      <p>Com o dinheiro recebido, os quatro países investiram principalmente em:</p>
      <br>
      <p><strong>• Educação</strong>: garantindo assim a erradicação do analfabetismo, o fim da evasão escolar, e a ampliação do conhecimento tecnológico. Tudo isso resultou em uma mão de obra extremamente qualificada.</p>
      <p><strong>• Infraestrutura</strong>: indústria de alta tecnologia, estradas, ferrovias e portos.</p>
      Atento ao rápido desenvolvimento dos Tigres Asiáticos, o Japão leva algumas de suas principais indústrias para esses países. O intuito dos japoneses era usufruir de um custo de produção mais baixo e produzir itens de valor competitivo no mercado.</p>
      <br>
      <p>Assim, pode-se afirmar que um dos fatores que mais contribuíram para a ascensão dos Tigres Asiáticos foram os investimentos privados internacionais, que resultaram no desenvolvimento da exportação.</p>

      <p>Os Estados disponibilizaram benefícios fiscais de forma a atrair as indústrias (exemplo: isenção ou redução de impostos). Essa medida teve importância fundamental na solidificação dos Tigres Asiáticos</p>

      <p>As políticas eram orientadas para a exportação de produtos e baseadas em uma gestão econômica preocupada com a produção em grande escala, com as taxas de juros e com a produtividade.</p>

      <p>Por volta da década de 1990, os Tigres Asiáticos deixam de atuar apenas como plataforma de exportação.</p>

      <p>Assim, em vez de apenas reproduzir tecnologias estrangeiras de produção e exportar, eles começaram a compreender as técnicas das indústrias japonesas.</p>

      <p>Com isso, passaram a produzir a sua própria tecnologia e os seus próprios produtos.</p>

      <p>Os <strong>principais produtos fabricados</strong> pelas indústrias dos Tigres Asiáticos são:</p>
      <br>
      <p>• Automóveis.</p>
      <p>• Brinquedos.</p>
      <p>• Computadores e periféricos.</p>
      <p>• Dispositivos de telecomunicação.</p>
      <p>• Eletroeletrônicos.</p>
      <p>• Máquinas industriais.</p>
      <p>• Navios.</p>
      <p>• Têxteis.</p>
      <br>
      <p>No entanto, havia uma escassez de matéria prima e além disso, a mão-de-obra ficou tão especializada que encareceu.</p>

      <p>Esse foi o grande ponto de virada na economia dos Tigres Asiáticos.</p>

      <p>Hong Kong, Singapura, Coreia do Sul e Taiwan sentiram necessidade de ter a sua própria plataforma de exportação e passaram a buscar países do sudeste asiático que pudessem servir de sede para suas indústrias.</p>

      <p>Assim, buscaram nações que tivessem mão de obra mais barata, ainda que não tão qualificada, de forma que o custo de produção fosse o mais baixo possível. Assim, surgem os Novos Tigres Asiáticos.</p>

      <h2>Características dos Tigres Asiáticos</h2>

      <p>Confira abaixo as principais características dos Tigres Asiáticos:</p>
      <br>
      <p>• Moeda desvalorizada.</p>
      <p>• Poucos recursos naturais.</p>
      <p>• Mercado nacional pequeno que fez com que a economia priorizasse o comércio com outros países.</p>
      <p>• Mão de obra barata e extremamente qualificada.</p>
      <p>• Alto investimento em educação.</p>
      <p>• Capacitação da mão de obra para qualificá-la.</p>
      <p>• Grande investimento na modernização da indústria.</p>
      <p>• Alto investimento na infraestrutura de transporte.</p>
      <p>• Adoção da plataforma de exportação como modelo de industrialização.</p>
      <p>• Aumento de salários.</p>
      <p>• Incentivos fiscais para exportações.</p>
      <p>• Isenção total de impostos na importação de máquinas e equipamentos industriais.</p>
      <p>• Industrialização para exportação para países mais ricos.</p>
      <p>• Rápido crescimento econômico.</p>

      <h2>Novos Tigres Asiáticos</h2>

      <p>O bloco dos Novos Tigres Asiáticos é formado por cinco países: Tailândia, Malásia, Indonésia, Vietnã e Filipinas.</p>

      <p>Também chamados de <strong>Tigres Asiáticos de segunda geração</strong>, tiveram ascensão econômica entre a década de 1990 e a década de 2000.</p>

      <p>Vietnã e Filipinas também são chamados de <strong>Novíssimos Tigres Asiáticos</strong>, pois foram os dois últimos a integrar o grupo.</p>

      <img src="https://static.todamateria.com.br/upload/no/vo/novostigresasiaticos-0-cke.jpg?auto_optimize=low" alt="">


      <p>Nessa época, alguns países do sudeste da Ásia, mais especificamente Japão, China e Coreia do Sul, ocupavam um lugar de destaque econômico bastante acentuado.</p>

      <p>Por esse motivo, voltaram as atenções de suas indústrias para produtos inovadores, de alta tecnologia e de alto valor agregado, ou seja, itens cuja produção demandava mais tempo e mais dedicação.</p>

      <p>Dessa forma, começaram a investir nos Novos Tigres Asiáticos e fizeram deles uma plataforma de exportação de produtos de baixo valor agregado, ou seja, de produtos básicos.</p>

      <p>Com o investimento das grandes potências, Tailândia, Malásia, Indonésia, Vietnã e Filipinas viveram uma transição: de territórios agrários passaram a territórios urbanos e industriais.</p>

      <p>Enquanto plataforma de exportação, os Novos Tigres Asiáticos conquistaram seu espaço no cenário econômico devido à industrialização com custo de produção baixo.</p>

      <p>Os <strong>principais produtos fabricados</strong> pelas indústrias dos Novos Tigres Asiáticos são:</p>
      <br>
      <p>• Alimentos.</p>
      <p>• Brinquedos.</p>
      <p>• Calçados.</p>
      <p>• Dispositivos eletrônicos.</p>
      <p>• Têxteis.</p>
      <br>
      <p>Embora o nível de industrialização tenha se desenvolvido significativamente, o mesmo não ocorreu com a tecnologia e a educação, principalmente se for estabelecida uma comparação com os Tigres Asiáticos.</p>

      <p>Esse fator é, certamente, um dos que comprometem o desenvolvimento dessas nações, pois resulta em uma formação acadêmica fraca.</p>

      <p>Diferentemente do que ocorreu nos Tigres Asiáticos, não houve erradicação do analfabetismo e nem da evasão escolar, por exemplo. Como consequência, nota-se que a mão de obra, embora seja volumosa e barata, não é tão qualificada.</p>

      <p>A produção industrial dos Novos Tigres Asiáticos não é tão significativa quanto a dos ditos Tigres Asiáticos de primeira geração, também chamados de Dragões Asiáticos.</p>

      <p>No entanto, os quatro países são ricos em matéria-prima e em volume de mão de obra, o que resulta em uma indústria de baixo custo.</p>


