      <h1><u>Renascimento</u></h1>

      <p>O <strong>Renascimento</strong> foi um movimento cultural, econômico e político, surgido na Itália no século XIV e se estendeu até o século XVII por toda a Europa.</p>

      <p>Inspirado nos valores da <strong>Antiguidade Clássica e gerado pelas modificações econômicas</strong>, o Renascimento reformulou a vida medieval e deu início à Idade Moderna.</p>

      <img src="https://static.todamateria.com.br/upload/re/na/renascimento-cultural-og.jpg" alt="" style="width: 70%;">

      <h2>Origem do Renascimento: conceito, mudanças e cidades renascentistas</h2>

      <p><strong>O termo Renascimento foi criado no século XVI para descrever o movimento artístico que surgiu um século antes</strong>. Posteriormente acabou designando as mudanças econômicas e políticas do período também e é muito contestado hoje em dia.</p>

      <p>O conceito "Renascimento" carrega a ideia de que esse período trouxe profundas e drásticas mudanças para o contexto europeu. Porém, apesar de ter sido um momento de alterações consideráveis para o velho continente, <strong>não houve uma total ruptura</strong> com as características existentes na Idade Média.</p>

      <p>Afinal, mesmo considerando as mudanças urbanas e no comércio, as cidades nunca desapareceram totalmente e os povos não deixaram de comercializar entre si, nem de usar moeda. Houve, sim, uma diminuição dessas atividades durante a Idade Média.</p>

      <p>Observamos, porém, que na Península Itálica várias cidades como <strong>Veneza, Gênova, Florença, Roma, dentre outras</strong>, se beneficiaram do comércio com o Oriente.</p>

      <p>Estas regiões enriqueceram com o desenvolvimento do comércio no Mar Mediterrâneo, dando origem a uma rica burguesia mercantil. A fim de se afirmarem socialmente, estes comerciantes patrocinavam artistas e escritores - uma prática chamada de <strong>mecenato</strong> - que inauguraram uma forma de fazer arte.</p>

      <p>A Igreja e nobreza também foram mecenas de artistas como Michelangelo, Domenico Ghirlandaio, Pietro della Francesa, entre muitos outros.</p>

      <h2>Cultura renascentista</h2>

      <p>Destacamos cinco características marcantes da cultura renascentista:</p>
      <br>
      <p>• Racionalismo - a razão era o único caminho para se chegar ao conhecimento. Tudo poderia ser explicado pela razão e pela ciência.</p>
      <p>• Cientificismo - todo conhecimento deveria ser demonstrado através da experiência científica.</p>
      <p>• Individualismo - o ser humano buscava afirmar a sua própria personalidade, mostrar seus talentos, atingir a fama e satisfazer suas ambições, através da concepção de que o direito individual estava acima do direito coletivo.</p>
      <p>• Antropocentrismo - o homem é visto como a suprema criação de Deus e como centro do universo. O homem agora é o centro do pensamento do próprio homem.</p>
      <p>• Classicismo - os artistas buscam sua inspiração na Antiguidade Clássica greco-romana para fazer suas obras.</p>

      <h2>Renascimento artístico</h2>


      <p>Os principais artistas do renascimento foram:</p>
      <br>
      <p><strong>Leonardo da Vinci</strong>: Matemático, físico, anatomista, inventor, arquiteto, escultor e pintor, ele foi o estereótipo do homem renascentista que domina várias ciências - a essa característica chamamos de <strong>universalismo</strong>. Por isso, é considerado um gênio absoluto. A Mona Lisa e A Última Ceia são suas obras-primas.</p>

      <img src="https://static.todamateria.com.br/upload/mo/na/monalisaleonardo-cke.jpg" alt="" style="width: 50%;">
      <h5>Mona Lisa</h5>

      <p><strong>Rafael Sanzio</strong>: foi um mestre da pintura e famoso por saber transmitir sentimentos delicados através de suas imagens de Nossa Senhora. Uma de suas obras principais é a Madona do Prado.</p>
      <br>
      <p><strong>Michelangelo</strong>: artista italiano cuja obra foi marcada pelo humanismo. Além de pintor, foi um dos maiores escultores do Renascimento. Entre suas obras destacam-se a Pietá, David, A Criação de Adão e O Juízo Final. Também foi o responsável por pintar o teto da Capela Sistina.</p>

      <h2>O Humanismo renascentista</h2>

      <p>O humanismo foi um movimento de glorificação do homem e da natureza humana, que surgiu nas cidades da <strong>Península Itálica</strong>, em meados do século XIV.</p>

      <p>O homem, a obra mais perfeita do Criador, seria capaz de compreender, modificar e até dominar a natureza. Por isso, os humanistas buscavam interpretar o cristianismo, utilizando escritos de autores da Antiguidade, como Platão.</p>

      <p>A religião não perdeu importância, mas passou a ser muito questionada. Assim surgiram novas correntes cristãs, como o protestantismo.</p>

      <p>O estudo dos textos antigos, igualmente, despertou o gosto pela pesquisa histórica e pelo conhecimento das línguas clássicas, como o latim e o grego.</p>

      <p>Desta forma, o humanismo se tornou referência para muitos pensadores nos séculos seguintes, como os filósofos iluministas do século XVII.</p>

      <h2>Renascimento literário</h2>

      <p>O Renascimento deu origem a grandes gênios da literatura, entre eles:</p>
      <br>
      <p><strong>• Dante Alighieri</strong>: escritor italiano, autor do grande poema "Divina Comédia".</p>
      <p><strong>• Maquiavel</strong>: autor de "O Príncipe", obra precursora da ciência política, onde o autor apresenta conselhos aos governadores da época.</p>
      <p><strong>• Shakespeare</strong>: considerado um dos maiores dramaturgos de todos os tempos. Abordou em sua obra os conflitos humanos nas mais diversas dimensões: pessoais, sociais, políticas. Escreveu comédias e tragédias, como "Romeu e Julieta", "Macbeth", "A Megera Domada", "Otelo" e várias outras.</p>
      <p><strong>• Miguel de Cervantes</strong>: autor espanhol da obra "Dom Quixote", uma crítica contundente da cavalaria medieval.</p>
      <p><strong>• Luís de Camões</strong>: teve destaque na literatura renascentista em Portugal, autor do grande poema épico "Os Lusíadas".</p>

      <h2>Renascimento científico</h2>

      <p>O <strong>Renascimento</strong> foi marcado por importantes descobertas científicas, notadamente nos campos da astronomia, da física, da medicina, da matemática e da geografia.</p>

      <p>O polonês <strong>Nicolau Copérnico</strong>, que negou a teoria geocêntrica defendida pela Igreja, ao afirmar que "a Terra não é o centro do universo, mas simplesmente um planeta que gira em torno do Sol".</p>

      <p><strong>Galileu Galilei</strong> descobriu os anéis de Saturno, as manchas solares, os satélites de Júpiter. Perseguido e ameaçado pela Igreja, Galileu foi obrigado a negar publicamente suas ideias e descobertas.</p>

      <p>Na medicina os conhecimentos avançaram com trabalhos e experiências sobre circulação sanguínea, métodos de cauterização e princípios gerais de anatomia.</p>


