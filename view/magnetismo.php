            <h2>O que é Magnetismo?</h2>

            <p><strong>Magnetismo</strong> é a propriedade de <strong>atração</strong> e <strong>repulsão</strong> de determinados <strong>metais</strong> e <strong>ímãs</strong>, que apresentam um polo positivo e outro negativo, caracterizados pelas <strong>“forças dipolo”</strong>.</p>
            <p>Dessa forma, a propriedade chamada de <strong>“dipolo magnético”</strong> informa que os polos iguais se repelem e os polos opostos se atraem.</p>

            <img src="https://s1.static.brasilescola.uol.com.br/be/conteudo/images/1-ma.jpg" alt="">
            <h5>O magnetismo é o que permite explicar a atração que os ímãs produzem sobre os metais.</h5>


            <h2>História do Magnetismo e do Eletromagnetismo</h2>



            <p>Sabe-se que o Magnetismo não é algo novo, uma vez que desde o século VII a. C. já eram utilizados seus conceitos; textos gregos apontam para a existência do magnetismo, propriedade de corpos presentes numa região denominada “Magnésia” e daí surgiu o nome da propriedade de atração e repulsão de determinados corpos.</p>

            <p><strong>Tales de Mileto</strong>, filósofo, físico e matemático grego (623 a.C. - 558 a.C.) foi quem observou a atração do ímã natural, a magnetita, com o ferro.</p>

            <p>Além disso, a invenção da <a href="">bússola</a>, que permitiu o avanço das navegações, já era utilizada pelos chineses desde século VII. Acredita-se que além de um instrumento, eles utilizavam-na como símbolo de sorte ou um oráculo.</p>

            <p>Alguns séculos depois, os estudos sobre o magnetismo e <a href="">eletromagnetismo</a> foram se expandindo. Isso aconteceu primeiramente em meados do século XIII, com <strong>Pierre Pelerin de Maricourt</strong>, o qual descreve sobre a bússola e as propriedades dos ímãs.</p>

            <p>Por conseguinte, no século XVI, <strong>Willian Gilbert </strong> (1544-1603) concluiu que a terra era magnética. Era por esse motivo que as bússolas sempre apontavam para o sentido norte.</p>

            <p>Em fins do século XVIII, <strong>Charles Coulomb</strong> (1736-1806) avançou nos estudos sobre eletricidade e magnetismo. Publicou a lei dos polos inversos de atração e repulsão entre as cargas elétricas.</p>

            <p>No século XIX, <strong>Hans Christian Oersted </strong> (1777-1851) publica trabalhos sobre o eletromagnetismo e os campos elétricos.</p>

            <p>Logo depois, entre 1821 e 1825, <strong>Andrè-Marie Ampère </strong> (1775-1836) realiza pesquisas sobre as correntes elétricas nos ímãs. Em homenagem a ele, o nome Ampère (A) foi eleita à unidade de medida da intensidade de corrente elétrica.</p>

            <p>Entretanto, foi <strong>Joseph Henry </strong> (1797-1878) e <strong>Michael Faraday </strong> (1791-1867) que descobrem a <a href="">indução eletromagnética</a>.</p>

            <p>Assim, 1865 foi o ano marco da era da eletricidade com a invenção do dínamo. Por meio da indução eletromagnética, o dínamo converte a <a href="">energia mecânica</a> em <a href="">energia elétrica</a>.</p>


            <h2>Ímã</h2>

            <p>O <a href="">ímã</a>, íman ou magneto é um corpo magnético (ferros magnetizados, rochas magnéticas) dipolo, ou seja, possui dois polos.</p>

            <p>Um dos polos é positivo e o outro é negativo. Eles possuem a propriedade de atrair outros corpos ferromagnéticos.</p>

            <p>São encontrados na natureza, em alguns minerais com propriedade magnéticas, por exemplo, a <strong>magnetita</strong>, ímã natural que atrai o ferro.</p>

            <p>Por outro lado, há o processo de fabricação dos ímãs artificiais, chamado de <strong>“imantação”</strong>, o qual ao corpo neutro é conferida a propriedade de atração magnética.</p>

            <p>Note que o ferro e algumas <a href="">ligas metálicas</a> são corpos que se imantam mais facilmente. Por isso, os ímãs artificiais são muito importantes na fabricação de aparelhos eletrônicos, <a href="">geradores elétricos</a>, bússolas, dentre outros.</p>


            <h2>Magnetismo Terrestre</h2>


            <p>O <strong>planeta Terra</strong> é considerado um <strong>grande ímã</strong>, dividido em <strong>dois polos</strong> (norte e sul), assemelhando-se a propriedade de <strong>dipolo magnético</strong>.</p>

            <p>Essa descoberta foi feita no século XVI, a partir das pesquisas do físico inglês William Gilbert. Note que o polo norte é o <a href="">campo magnético</a> que sempre atrai a bússola, o que explica que a Terra comporta-se como um grande ímã que exerce força de atração na direção norte.</p>


