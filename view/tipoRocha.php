      <h1><u>Tipos de Rochas</u></h1>

      <p>Os três tipos de rocha existentes são as: <strong>rochas magmáticas</strong>, <strong>rochas sedimentares</strong> e <strong>rochas metamórficas</strong>.</p>

      <p>Lembre-se que rocha é um agregado natural formado por um ou mais minerais. Seu processo de formação é contínuo e as primeiras rochas surgiram após a formação e resfriamento da Terra.</p>

      <p>Ao longo da história geológica da Terra, as rochas se formam e se modificam constantemente. Rochas antigas são transformadas em rochas novas. Enquanto isso, novas rochas são criadas pelo derramamento de magma. É o chamado "ciclo das rochas".</p>

      <h2>Origem e Classificação das Rochas</h2>


      <p>Na Era Primitiva ou Pré-Cambriana a Terra devia ser uma só massa incandescente, com temperaturas elevadíssimas, sem existência de matéria sólida.</p>

      <p>Os minerais eram uma massa pastosa, semelhante ao magma. Quando a Terra começou o processo de esfriamento, muitos minerais se solidificaram e formaram as primeiras rochas do planeta - as <strong>rochas magmáticas</strong>.</p>

      <p>Os gases e vapores que escaparam do resfriamento dos minerais deram origem à camada de ar que envolve a Terra: a atmosfera.</p>

      <p>Com a formação das chuvas, dos rios e oceanos, agindo como agentes de erosão, foram se formando novas formas de relevo.</p>

      <p>Os detritos (sedimentos) resultados das erosões das rochas primitivas foram sendo depositados, camadas por camadas, nas depressões, dando origem às <strong>rochas sedimentares</strong>.</p>

      <p>Submetidas às condições de temperatura e pressão, as rochas magmáticas e sedimentares deram origem às <strong>rochas metamórficas</strong>.</p>

      <h3>Rochas Magmáticas</h3>

      <img src="https://static.todamateria.com.br/upload/ro/ch/rochasmagmaticas-cke.jpg" alt="" style="width: 55%;">

      <p>A <strong>rochas magmáticas</strong>, também chamadas de <strong>ígneas</strong>, são formadas pelo resfriamento e solidificação do magma pastoso. O magma que existe no interior da terra é expelido pelas erupções vulcânicas.</p>

      <p>A solidificação do magma ocorre de duas maneiras: na superfície e no interior da Terra.</p>

      <p>O magma que chega à superfície e sofre rápido resfriamento permite que se formem pequenos cristais na sua composição, não visíveis a olho nu. Recebem o nome de <strong>rochas magmáticas vulcânicas</strong> ou <strong>extrusivas</strong>.</p>

      <p>O resfriamento no interior da terra forma as rochas <strong>magmáticas plutônicas</strong> ou <strong>intrusivas</strong>. Nesse caso o resfriamento do magma é lento, permitindo a formação de grandes cristais, visíveis a olho nu. São também chamadas de <strong>rochas cristalinas</strong>.</p>

      <p>São <strong>exemplos</strong> de rochas magmáticas:</p>
      <br>
      <p>• O <strong>basalto</strong>, que é o tipo de rocha magmática mais comum. É utilizado como paralelepípedo para o calçamento de ruas;</p>
      <p>• O <strong>granito</strong>, que quando polido é usado no revestimento de pisos, paredes e tampo de pia de cozinhas e de banheiros. Sem o polimento é usado como calçamento de ruas;</p>
      <p>• O <strong>diorito</strong>, cuja finalidade é especialmente fazer pedra britada para construção de estradas.</p>

      <h3>Rochas Sedimentares</h3>

      <img src="https://static.todamateria.com.br/upload/ro/ch/rochassedimentares-cke.jpg?auto_optimize=low" alt="" style="width: 55%;">

      <p>As <strong>rochas sedimentares</strong> resultam da deposição de detritos de outras rochas ou de matérias orgânicas em depressões do relevo terrestre.</p>

      <p>A ação das chuvas, dos ventos, dos rios, mares e geleiras sobre o relevo, desgasta as rochas da superfície terrestre.</p>

      <p>Esses processos vão formando sedimentos que são transportados para as partes mais baixas do relevo, dos mares, lagos e rios.</p>

      <p>No processo de formação das rochas sedimentares, os detritos se acumulam e se consolidam em camadas de estratos.</p>

      <p>As rochas sedimentares são também chamadas de <strong>rochas estratificadas</strong>, pois se apresentam em camadas de sedimentos.</p>

      <p>A formação de <strong>petróleo</strong> originou-se da deposição de micro-organismos em <strong>bacias sedimentares</strong>. Estas podem existir tanto nos continentes quanto nos oceanos.</p>

      <p>A deposição dos sedimentos ocorre também por meio de processos químicos, como as estalactites e estalagmites das grutas calcárias.</p>

      <p>As estalactites são formas que pendem do teto e as estalagmites são provenientes de pingos d'água que se acumulam no chão. Ambas são formadas por bicarbonato de sódio dissolvido em água.</p>

      <p>São <strong>exemplos</strong> de rochas sedimentares:</p>
      <br>
      <p>• O <strong>arenito</strong>, que é empregado na fabricação de vidros;</p>
      <p>• O <strong>argilito</strong>, feito de argila, que é empregada na fabricação de tijolos e telhas;</p>
      <p>• O <strong>carvão mineral</strong>, que é utilizado como combustível.</p>

      <h3>Rochas Metamórficas</h3>

      <img src="https://static.todamateria.com.br/upload/ro/ch/rochasmetamorficas-cke.jpg?auto_optimize=low" alt="" style="width: 55%;">


      <p>As <strong>rochas metamórficas</strong> têm sua origem na transformação de outras rochas (magmáticas e sedimentares), quando submetidas a certas condições de umidade, calor e pressão no interior da Terra.</p>

      <p>A rocha transformada adquire novas características e tem sua composição alterada.</p>

      <p>São exemplos de rochas metamórficas:</p>

      <p>• O <strong>mármore</strong>, que é bastante utilizado na construção e na criação de monumentos;</p>
      <p>• O <strong>quartzito</strong>, utilizado para fins ornamentais, é uma rocha parecida com o mármore, porém, mais resistente.</p>
      <p>• O <strong>gnaisse</strong>, além de ser utilizada na ornamentação, é utilizada também na construção civil.</p>



