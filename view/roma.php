      <h1><u>Roma Antiga</u></h1>


      <p>A <strong>cidade de Roma</strong> nasceu como uma pequena aldeia e se tornou um dos maiores impérios da Antiguidade.</p>

      <p>Situada na Península Itálica, centro do Mediterrâneo europeu, Roma era o centro da vida política e econômica da região.</p>


      <h2>Fundação de Roma</h2>


      <p>A <strong>fundação de Roma</strong> está envolta em lendas. Segundo a narrativa do <strong>poeta Virgílio</strong>, em sua obra Eneida, os romanos descendem de Enéias, herói troiano, que fugiu para a Itália após a destruição de Troia pelos gregos, por volta de <strong>1400 a.C.</strong></p>

      <p>Segundo a lenda, os gêmeos <strong>Rômulo e Remo</strong>, descendentes de Enéias, foram jogados no rio Tibre, por ordem de Amúlio, usurpador do trono.</p>

      <img src="https://static.todamateria.com.br/upload/ro/mu/romuloeremolobabb.jpg" alt="" style="width: 55%;">



      <p>Amamentados por uma loba e depois criados por um camponês, os irmãos voltam para destronar Amúlio.</p>

      <p>Os irmãos receberam a missão de fundar Roma, em <strong>753 a.C.</strong> Rômulo, após desentendimentos, assassinou Remo e se transformou no <strong>primeiro rei de Roma</strong>.</p>

      <p>Na realidade, Roma formou-se da fusão de sete pequenas aldeias de pastores latinos e sabinos situadas às margens do rio Tibre. Depois de conquistada pelos etruscos chegou a ser uma verdadeira cidade-Estado.</p>

      <h2>Monarquia Romana (753 a.C. a 509 a.C.)</h2>


      <p>Na <strong>Roma monárquica</strong>, a sociedade era formada basicamente por três classes sociais:</p>
      <br>
      <p><strong>• Os patrícios</strong>, a classe dominante, formada por nobres e proprietários de terra;</p>
      <p><strong>• Os plebeus</strong>, que eram constituídos por comerciantes, artesãos, camponeses e pequenos proprietários;</p>
      <p><strong>• Os clientes</strong>, que viviam da dependência dos patrícios e os plebeus, e eram prestadores de serviços.</p>
      <br>
      <p>Na monarquia romana, o rei exercia funções <strong>executiva</strong>, <strong>judicial</strong> e <strong>religiosa</strong>.</p>

      <p>Era assistido pela Assembleia Curiata, que estava formada por trinta chefes de famílias do povo. Sua função mudou ao longo dos séculos, mas eram responsáveis por elaborar leis, recursos jurídicos e ratificar a eleição do rei. Em certos períodos a Assembleia Curiata deteve mais poder que o Senado.</p>

      <p> O Senado, composto pelos patrícios, assessorava o rei e tinha o poder de vetar as leis apresentadas pelo monarca.</p>

      <p>As lendas narram os acontecimentos dos sete reinados da época. Durante o governo dos três últimos, que eram etruscos, o poder político dos patrícios declinou.</p>

      <p>A aproximação dos reis com a plebe descontentavam os patrícios. Em 509 a.C., o último rei etrusco foi deposto e um golpe político marcou o fim da monarquia.</p>


      <h2>República Romana (509 a.C. a 27 a.C.)</h2>


      <p>A <strong>implantação da república</strong> significou a afirmação do Senado, o órgão de maior poder político entre os romanos. O poder executivo ficou a cargo das magistraturas, ocupadas pelos patrícios.</p>

      <p>A república romana foi marcada pela luta de classes entre patrícios e plebeus. Os patrícios lutavam para preservar privilégios e defender seus interesses políticos e econômicos, mantendo os plebeus sob sua dominação.</p>

      <p>Entre <strong>449 e 287 a.C.</strong> os plebeus organizaram cinco revoltas que resultaram em várias conquistas: <strong>Tribunos da plebe, Leis das XII tábuas, Leis Licínias e Lei Canuleia</strong>. Com essas medidas, as duas classes praticamente se igualaram.</p>
      <br>
      <h3>A Expansão Romana</h3>
      <img src="https://static.todamateria.com.br/upload/gu/er/guerrapunicaelefantebb.jpg?auto_optimize=low" alt="" style="width: 45%;">

      <h5>Durante a Guerra Púnica foram utilizados elefantes como animais de combate</h5>



      <p>A primeira etapa das conquistas romanas foi marcada pelo domínio de toda a <strong>Península Ibérica</strong> a partir do século <strong>IV a.C.</strong></p>

      <p>A segunda etapa foi o início das Guerras de Roma contra Cartago, chamadas Guerras Púnicas (264 a 146 a.C.). Em 146 a.C. Cartago foi totalmente destruída. Em pouco mais de cem anos, toda a bacia do Mediterrâneo já era de Roma.</p>

      <br>
      <h3>Crise da República</h3>

      <p>Na <strong>República romana</strong>, a escravidão era a base de toda produção e o número de escravos ultrapassava os de homens livres. A violência contra os escravos causou dezenas de revoltas.</p>

      <p>Uma das principais revoltas escravos foi liderada por Espártaco entre <strong>73 a 71 a.C.</strong> À frente das forças rebeldes, Espártaco ameaçou o poder de Roma.</p>



      <p>Para equilibrar as forças políticas, em <strong>60 a.C.</strong>, o Senado indicou três líderes políticos ao consulado, Pompeu, Crasso e Júlio César, que formaram o primeiro Triunvirato.</p>

      <p>Após a morte de Júlio César, foi instituído o segundo Triunvirato constituído por Marco Antônio, Otávio Augusto e Lépido.</p>

      <p>As disputas de poder eram frequentes. Otávio recebeu do senado o título de Prínceps (primeiro cidadão) foi a primeira fase do império disfarçado de República.</p>


      <h2>
        Império Romano (27 a.C. a 476)
      </h2>

      <img src="https://static.todamateria.com.br/upload/im/pe/imperioromanoccd-cke.jpg?auto_optimize=low" alt="" style="width: 70%;">
      <h5>Mapa dos territórios dominados pelo Império Romano por volta de 70 d.C.</h5>

      <p>O <strong>imperador Otávio Augusto (27 a.C. a 14)</strong> reorganizou a sociedade romana. Ampliou a distribuição de pão e trigo e de divertimentos públicos - a política do pão e circo.</p>

      <p>Depois de Augusto, várias dinastias se sucederam. Entre os principais imperadores estão:</p>
      <br>

      <p><strong>• Tibério (14 a 37);</strong></p>
      <p><strong>• Calígula (37 a 41);</strong></p>
      <p><strong>• Nero (54 a 68);</strong></p>
      <p><strong>• Tito (79 a 81);</strong></p>
      <p><strong>• Trajano (98 a 117);</strong></p>
      <p><strong>• Adriano (117-138);</strong></p>
      <p><strong>• Marco Aurélio (161 a 180).</strong></p>

      <h2>Decadência do Império Romano</h2>


      <p>A partir de <strong>235</strong>, o Império começou a ser governado pelos <strong>imperadores-soldados</strong>, cujo principal objetivo era combater as invasões.</p>

      <p>Do ponto de vista político, o <strong>século III</strong> caracterizou-se pela volta da anarquia militar. Num período de apenas meio século (235 a 284) Roma teve 26 imperadores, dos quais 24 foram assassinados.</p>

      <p>Com a morte do imperador Teodósio, em 395, o Império Romano foi dividido entre seus filhos Honório e Arcádio.</p>


      <p>Honório ficou com o Império Romano do Ocidente, capital Roma, e Arcádio ficou com o Império Romano do Oriente, capital Constantinopla.</p>

      <p>Em 476, o Império Romano do Ocidente desintegrou-se e o imperador Rômulo Augusto foi deposto. O ano de 476 é considerado pelos historiadores o marco divisório da Antiguidade para a Idade Média.</p>

      <p>Da poderosa Roma, restou apenas o Império Romano do Oriente, que se manteria até 1453.</p>


      <h2>Curiosidades</h2>


      <p>• Devido à expansão territorial, durante o império, os romanos passaram a representar 25% da população mundial.</p>
      <p>• Os canhotos eram vistos como pessoas de má-sorte e não confiáveis. Esta crença permaneceu até pouco tempo, quando as crianças eram obrigadas a escrever com a mão direita.</p>
      <p>• Os romanos prezavam muito pela higiene. As classes abastadas tinham água encanada em casa e os pobres possuíam fontes perto de suas residências. Igualmente, iam regularmente aos banhos públicos.</p>
      <p>• A urina era aproveitada para diversos fins por conta do ácido e outros componentes: usavam para clarear os dentes, lavar a roupa e fazer moedas.</p>









