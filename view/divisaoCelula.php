            <h1><u>Divisão Celular</u></h1>

            <p>A <strong>divisão celular</strong> é o processo pelo qual uma célula-mãe origina <strong>células-filhas</strong>.</p>

            <p>Através deste processo as células unicelulares se reproduzem e as multicelulares se multiplicam.</p>

            <p>A frequência de divisões celulares varia com o tipo e estado fisiológico de cada célula.</p>

            <p>No organismo humano, por exemplo, algumas células estão em constante multiplicação. Um exemplo são as células da epiderme e da medula óssea, que se multiplicam para repor as células que morrem.</p>

            <p>Entretanto, alguns tipos de células mais especializadas como os neurônios, hemácias e células musculares, nunca se dividem.</p>

            <img src="https://static.todamateria.com.br/upload/di/vi/divisao-celular-og.jpg" alt="" style="width: 75%;">
            <h2>Ciclo Celular
            </h2>

            <p>É o período que se inicia com a origem da célula, a partir de uma divisão celular e termina quando esta se divide em duas células-filhas.</p>

            <p>O ciclo celular é dividido em duas etapas: a <strong>interfase</strong> e a <strong>divisão celular</strong>.</p>

            <p>Nos eucariontes existem dois tipos de divisão celular: a <strong>mitose</strong> e a <strong>meiose</strong>.</p>

            <img src="https://static.todamateria.com.br/upload/13/63/1363683939297697fb96o.jpg" alt="" style="width: 45%;">
            <h5>Ciclo Celular: interfase e mitose</h5>

            <h2>Interfase</h2>

            <p>É a fase em que a célula não está se dividindo.</p>

            <p>É o período mais longo do ciclo celular, aproximadamente 95% do tempo.</p>

            <p>Neste momento ocorrem diversos fatos que possibilitam a divisão celular, como: a replicação do DNA, a divisão dos centríolos e a produção de proteínas.</p>

            <p>A interfase é subdividida em três fases: G1, S e G2.</p>

            <p>Na fase G1, que antecede a duplicação do DNA, as células aumentam de tamanho, produzem RNA e sintetizam proteínas.</p>

            <p>Na fase S ocorre a síntese de DNA. A quantidade de DNA no núcleo da célula é replicado. Lembre-se que replicação significa o processo de duplicação da molécula de DNA.</p>

            <p>Antes de qualquer divisão celular há duplicação do DNA durante a interfase.</p>

            <p>A fase G2, corresponde ao intervalo entre a síntese de DNA e a mitose. A célula continua crescendo e produzindo proteínas.</p>

            <h2>Tipos de Divisão Celular</h2>

            <h3>Mitose</h3>

            <p>É o tipo de divisão celular que a célula-mãe, haploide (n) ou diploide (2n), origina 2 células-filhas com o mesmo número de <strong>cromossomos</strong> da célula-mãe.</p>

            <p>É uma divisão equacional.</p>

            <p>A mitose é realizada quando há reprodução assexuada.</p>

            <h3>Funções da mitose</h3>

            <p>• Crescimento e regeneração de tecidos;</p>
            <p>• Cicatrização;</p>
            <p>• Formação de gametas em vegetais;</p>
            <p>• Divisões do zigoto durante o desenvolvimento embrionário.</p>

            <h3>Meiose</h3>

            <p>É o tipo de divisão celular em que a célula mãe, sempre diploide (2n), com cromossomos duplos, origina através de duas divisões sucessivas, quatro células filhas com metade do número de cromossomos da célula mãe.</p>

            <p>É uma divisão do tipo reducional.</p>

            <h3>Funções da Meiose</h3>

            <p>• Formação dos gametas em animais;</p>
            <p>• Formação dos esporos nos vegetais.</p>

