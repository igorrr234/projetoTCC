      <h1><u>Idade Média</u></h1>

      <p>A <strong>Idade Média</strong> foi um longo período da história que se estendeu do século V ao século XV. <strong>Seu início foi marcado pela Queda do Império Romano do Ocidente, em 476 d.C., e o fim, pela Tomada de Constantinopla pelos turcos-otomanos, em 1453</strong>.</p>

      <p>Foi também nesse período que surgiu e se consolidou o feudalismo, organização política, econômica e social baseada na posse da terra.</p>

      <p>Os <strong>humanistas</strong> do século XV e XVI chamavam a Idade Média de <strong>Idade das Trevas</strong>. Eles afirmavam haver ocorrido na Europa, um retrocesso artístico, intelectual, filosófico e institucional, em relação à produção da Antiguidade Clássica.</p>

      <img src="https://static.mundoeducacao.uol.com.br/mundoeducacao/2020/09/carlos-magno.jpg" alt="" style="width: 70%;">
      <h2>Características da Idade Média</h2>

      <p>Os historiadores dividem o período medieval em duas partes: a <strong>Alta Idade Média</strong> e a <strong>Baixa Idade Média</strong>. Veja abaixo as principais características de cada período.</p>

      <h3>Alta Idade Média</h3>

      <p>A <strong>Alta Idade Média</strong> foi um período de instabilidade e insegurança generalizada, que se estendeu do século V ao século X. Nesse período destacam-se:</p>
      <br>
      <p><strong>• Os Reinos Germânicos</strong> – os germânicos eram povos estabelecidos ao longo das fronteiras do Império Romano. Os romanos os chamavam de "bárbaros”, por serem estrangeiros e não falarem o latim. Os germanos formaram vários Reinos Germânicos dentro do território romano;</p>
      <p><strong>• O Reino Cristão dos Francos</strong> – o reino dos francos constituiu o reino mais poderoso da Europa Ocidental;</p>
      <p><strong>• A Igreja e o Sacro Império</strong> – A Igreja Medieval teve importante papel na sociedade. Foi nessa época que começou a organizar-se, com o objetivo de zelar pela homogeneidade dos princípios da religião cristã e promover a conversão dos pagãos.</p>
      <p><strong>• O Sistema Feudal</strong> – o <a href="feudalismo.php">feudalismo</a> começou a se formar no século V, na Europa Ocidental, com a crise do Império Romano.</p>
      <p><strong>• O Império Bizantino</strong> – estabelecido em Constantinopla, o Império Bizantino sobreviveu a invasões bárbaras e perdurou por todo o período medieval.</p>
      <p><strong>• Os Árabes e o Islamismo</strong> – no Oriente Médio, na península arábica, nasceu em 630 o Islão, como resultado das Guerras Santas empreendidas por Maomé. Lentamente, o Islamismo se expandiu por um extenso território, conquistando terras da Ásia, África e Europa.</p>

      <h3>Baixa Idade Média</h3>


      <p>A <strong>Baixa Idade Média</strong> é o período que vai do século XI ao século XV. Destacam-se nessa época:</p>
      <br>
      <p>• Crise do feudalismo</p>
      <p>• As cruzadas e a expansão das sociedades cristãs;</p>
      <p>• O ressurgimento urbano na Europa;</p>
      <p>• O renascimento comercial europeu;</p>
      <p>• A formação das monarquias nacionais europeias;</p>
      <p>• A cultura medieval.</p>
      <p>Durante a Baixa Idade Média, com a expansão dos turcos-otomanos no século XIV, tomando os Bálcãs e a Ásia Menor, o Império Bizantino acabou reduzido à cidade de Constantinopla.</p>

      <p>A queda em 1453, foi um fato histórico que marcou o <strong>fim da Idade Média na Europa</strong>. A conquista da capital bizantina pelo Império Otomano sob o comando do sultão Maomé II, marcou o fim do Império Romano no Ocidente.</p>

      <h2>Por que a Idade Média recebeu esse nome?</h2>



      <p>Os pensadores do período moderno, chamados humanistas, durante o período de transição entre as idades média e moderna, iniciaram um movimento de retomada aos valores e estudos greco-romanos, tendo como norte a valorização do ser humano - o antropocentrismo.</p>

      <p>Assim, passaram a compreender o período anterior como um momento de trevas, um meio onde a racionalidade da Antiguidade Clássica se foi abandonada pelo ser humano, até se reencontrar durante a Idade Moderna.</p>



