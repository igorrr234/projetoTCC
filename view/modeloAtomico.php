            <h1><u>Modelos Atômicos</u></h1>

            <p>Os <strong>modelos atômicos</strong> são os aspectos estruturais dos átomos que foram apresentados por cientistas na tentativa de compreender melhor o átomo e a sua composição.</p>

            <p>Em 1808, o cientista inglês John Dalton propôs uma explicação para a propriedade da matéria. Trata-se da primeira teoria atômica que dá as bases para o modelo atômico conhecido atualmente.</p>

            <p>A constituição da matéria é motivo de estudos desde a antiguidade. Os pensadores <strong>Leucipo</strong> (500 a.C.) e <strong>Demócrito</strong> (460 a.C.) formularam a ideia de haver um limite para a pequenez das partículas.</p>

            <p>Eles afirmavam que elas se tornariam tão pequenas que não poderiam ser divididas. Chamou-se a essa partícula última de átomo. A palavra é derivada dos radicais gregos que, juntos, significam o que não se pode dividir.</p>

            <h2>Modelo Atômico de Dalton</h2>

            <img src="https://static.todamateria.com.br/upload/da/lt/dalton.jpg" alt="" style="width: 40%;">
            <h5>Modelo atômico de Dalton</h5>

            <p>O <strong>Modelo Atômico de Dalton</strong>, conhecido como o modelo bola de bilhar, possui os seguintes princípios:</p>
            <p>• Todas as substâncias são formadas de pequenas partículas chamadas átomos;</p>
            <p>• Os átomos de diferentes elementos têm diferentes propriedades, mas todos os átomos do mesmo elemento são exatamente iguais;</p>
            <p>• Os átomos não se alteram quando formam componentes químicos;</p>
            <p>• Os átomos são permanentes e indivisíveis, não podendo ser criados nem destruídos;</p>
            <p>• As reações químicas correspondem a uma reorganização de átomos.</p>


            <h2>Modelo Atômico de Thomson</h2>

            <img src="https://static.todamateria.com.br/upload/th/om/thomson.jpg?auto_optimize=low" alt="" style="width: 35%;">
            <h5>Modelo Atômico de Thomson</h5>

            <p>O <strong>Modelo Atômico de Thomson</strong> foi o primeiro a realizar a divisibilidade do átomo. Ao pesquisar sobre raios catódicos, o físico inglês propôs esse modelo que ficou conhecido como o modelo pudim de ameixa.</p>

            <p>Ele demonstrou que esses raios podiam ser interpretados como sendo um feixe de partículas carregadas de energia elétrica negativa.</p>

            <p>Em 1887, Thomson sugeriu que os elétrons eram um constituinte universal da matéria. Ele apresentou as primeiras ideias relativas à estrutura interna dos átomos.</p>

            <p>Thomson indicava que os átomos deviam ser constituídos de cargas elétricas positivas e negativas distribuídas uniformemente.</p>

            <p>Ele descobriu essa mínima partícula e assim estabeleceu a teoria da natureza elétrica da matéria. Concluiu que os elétrons eram constituintes de todos os tipos de matéria, pois observou que a relação carga/massa do elétron era a mesma para qualquer gás empregado em suas experiências.</p>

            <p>Em 1897, Thomson tornou-se reconhecido como o <strong>“pai do elétron”</strong>.</p>

            <h2>Modelo Atômico de Rutherford</h2>

            <img src="https://static.todamateria.com.br/upload/ru/th/rutherford2.jpg?auto_optimize=low" alt="" style="width: 40%;">
            <h5>Modelo atômico de Rutherford</h5>


            <p>Em 1911, o físico neozelandês Rutherford colocou uma folha de ouro bastante fina numa câmara metálica. Seu objetivo era analisar a trajetória de partículas alfa a partir do obstáculo criado pela folha de ouro.</p>
            <p>Nesse ensaio de Rutherford, observou que algumas partículas ficavam totalmente bloqueadas e outras partículas, que não eram afetadas, ultrapassavam a folha, sofrendo desvios. Segundo ele, esse comportamento podia ser explicados graças às forças de repulsão elétrica entre essas partículas.</p>

            <p>Pelas observações, afirmou que o átomo era nucleado e sua parte positiva se concentrava num volume extremamente pequeno, que seria o próprio núcleo.</p>

            <p>O <strong>Modelo Atômico de Rutherford</strong>, conhecido como modelo planetário, corresponde a um sistema planetário em miniatura, no qual os elétrons se movem em órbitas circulares, ao redor do núcleo.</p>

            <h2>Modelo de Rutherford – Bohr</h2>

            <img src="https://static.todamateria.com.br/upload/ru/th/rutherforborh-cke.jpg?auto_optimize=low" alt="" style="width: 40%;">
            <h5>Modelo Atômico de Rutherford-Bohr</h5>

            <p>O <strong>modelo apresentado por Rutherford foi aperfeiçoado por Bohr</strong>. Por esse motivo, o aspecto da estrutura atômica de Bohr também é chamada de <strong>Modelo Atômico de Bohr</strong> ou Modelo Atômico de Rutherford-Bohr.</p>

            <p>A teoria do físico dinamarquês Niels Bohr estabeleceu as seguintes concepções atômicas:</p>

            <p>• Os elétrons que giram ao redor do núcleo não giram ao acaso, mas descrevem órbitas determinadas.</p>
            <p>• O átomo é incrivelmente pequeno, mesmo assim a maior parte do átomo é espaço vazio. O diâmetro do núcleo atômico é cerca de cem mil vezes menor que o átomo todo. Os elétrons giram tão depressa que parecem tomar todo o espaço.</p>
            <p>• Quando a eletricidade passa através do átomo, o elétron pula para a órbita maior e seguinte, voltando depois à sua órbita usual.</p>
            <p>• Quando os elétrons saltam de uma órbita para a outra resulta luz. Bohr conseguiu prever os comprimentos de onda a partir da constituição do átomo e do salto dos elétrons de uma órbita para a outra.</p>



