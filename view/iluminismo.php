      <h1><u>Iluminismo</u></h1>

      <p>O <strong>Iluminismo</strong> foi um movimento intelectual que se tornou popular no século XVIII, conhecido como "Século das Luzes".</p>

      <p>Surgido na França, a principal característica desta corrente de pensamento foi defender o uso da razão sobre o da fé para entender e solucionar os problemas da sociedade.</p>

      <img src="https://static.todamateria.com.br/upload/il/um/iluminismo-og.jpg" alt="" style="width: 70%;">

      <h2>Resumo sobre Iluminismo</h2>

      <p>Os iluministas acreditavam que poderiam reestruturar a sociedade do Antigo Regime. Defendiam o poder da razão acima da fé e da religião e buscaram estender a crítica racional em todos os campos do saber humano.</p>

      <p>Através da união de escolas de pensamento filosóficas, sociais e políticas, enfatizavam a defesa do conhecimento racional para desconstruir preconceitos e ideologias religiosas.</p>

      <p>Por sua vez, essas seriam superadas pelas ideias de progresso e perfectibilidade humana.</p>

      <p>Em suas obras, os pensadores iluministas argumentavam contra as determinações mercantilistas e religiosas.</p>

      <p>Também foram avessos ao absolutismo e aos privilégios dados à nobreza e ao clero. Estas ideias eram consideradas polêmicas, pois isso abalava os alicerces da estrutura política e social do Antigo Regime.</p>

      <p>Desta maneira, filósofos como Diderot e D’Alembert buscaram reunir todo o conhecimento produzido à luz da razão num compêndio dividido em 35 volumes: a Enciclopédia (1751-1780).</p>

      <p>A publicação da Enciclopédia contou com a participação de vários expoentes iluministas como Montesquieu e Jean-Jacques Rousseau.</p>

      <p>Suas ideias se difundiram principalmente entre a burguesia, que detinha a maior parte do poder econômico. Entretanto, não possuíam nada equivalente em poder político e ficavam sempre à margem das decisões.</p>

      <h2>Características do Iluminismo</h2>

      <p>O iluminismo rejeitava a herança medieval e, por isso, passaram a chamar este período de "Idade das Trevas". Foram esses pensadores que inventaram a ideia que nada de bom havia acontecido nesta época.</p>

      <p>Vejamos, a seguir, as principais ideias iluministas sobre economia, política e religião.</p>

      <h3>Economia</h3>

      <p>Em oposição ao <strong>Mercantilismo</strong>, praticado durante o Antigo Regime, os iluministas afirmavam que o Estado deveria praticar o liberalismo. Ao invés de intervir na economia, o Estado deveria deixar que o mercado a regulasse. Essas ideias foram expostas, principalmente, por Adam Smith.</p>

      <p>Alguns, como Quesnay, defendiam que a agricultura era a fonte de riqueza da nação, em detrimento do comércio, como defendido pelos mercantilistas.</p>

      <p>Quanto à propriedade privada, não havia consenso entre os iluministas. John Locke enfatizava que a propriedade era um direito natural do homem, enquanto Rousseau, apontava que esta era a razão dos males da humanidade.</p>

      <h3>Política e Sociedade</h3>

      <p>Contrários ao <strong>Absolutismo</strong>, os iluministas afirmavam que o poder do rei deveria ser limitado por um conselho ou uma Constituição.</p>

      <p> O escritor Montesquieu, por exemplo, defendia um modelo de Estado onde o governo estaria dividido em três poderes:</p>
      <br>
      <p>• Legislativo</p>
      <p>• Executivo</p>
      <p>• Judiciário.</p>
      <br>
      <p>Assim, haveria equilíbrio e menos poder concentrado numa só pessoa. Esta ideia de governo foi adotada por quase todos os países do mundo ocidental.</p>

      <p>Igualmente, os súditos deveriam ter mais direitos e serem tratados de forma igualitária. Com isso queria se afirmar que todos deveriam pagar impostos e minorias, como os judeus, tinham que ser reconhecidos como cidadãos plenos.</p>

      <p>É preciso lembrar que no Antigo Regime, as minorias religiosas, como judeus e muçulmanos, foram obrigados a se converter ou a deixar os países onde estavam para escapar das perseguições.</p>

      <p>Embora houvesse algumas vozes a favor das mulheres e até pensadoras iluministas, como Émilie du Châtelet ou Mary Wollstonecraft, nenhum homem defendeu realmente a concessão de direitos para elas.</p>

      <h3>Religião</h3>

      <p>A religião foi muito criticada por vários pensadores iluministas.</p>

      <p>A maioria, defendia a limitação dos privilégios do Clero e da Igreja, bem como o uso da ciência para questionar as doutrinas religiosas.</p>

      <p>Havia aqueles que compreendiam o poder da religião na formação do ser humano, mas preferiam que houvessem duas esferas distintas: a religião e o Estado.</p>

      <p>De igual maneira, alguns iluministas defendiam o fim da igreja como instituição e que a fé deveria ser uma expressão individual.</p>

      <h2>Principais pensadores iluministas</h2>

      <p>Segue abaixo os principais <strong>filósofos iluministas</strong>:</p>
      <br>
      <p>• Montesquieu (1689-1755)</p>
      <p>• Voltaire (1694-1778)</p>
      <p>• Diderot (1713-1784)</p>
      <p>• D’Alembert (1717-1783)</p>
      <p>• Rousseau (1712-1778)</p>
      <p>• John Locke (1632-1704)</p>
      <p>• Adam Smith (1723-1790)</p>

      <h2>Despotismo esclarecido</h2>

      <p>As ideias iluministas se espalharam de tal modo que muitos governantes buscaram implantar medidas embasadas no iluminismo para modernizar seus respectivos Estados.</p>

      <p>Isso acontecia sem que os monarcas abdicassem de seu poder absoluto, apenas o conciliando aos interesses populares. Deste modo, estes governantes faziam parte do <strong>Despotismo Esclarecido</strong>.</p>

      <h2>Iluminismo no Brasil</h2>

      <p>O Iluminismo chegou ao Brasil através das publicações que eram contrabandeadas para a colônia.</p>

      <p>Igualmente, vários estudantes que iam à Universidade de Coimbra, em Portugal, também tiveram contato com as ideias iluministas e passaram a difundi-las.</p>

      <p>Essas ideias passavam a questionar o próprio sistema colonial e fomentar o desejo de mudanças. Assim, o movimento das Luzes influenciou a Inconfidência Mineira (1789), a Conjuração Baiana (1798) e a Revolução Pernambucana (1817).</p>

      <h2>Consequências do Iluminismo</h2>

      <p>Os ideais iluministas tiveram sérias implicações sociopolíticas. Como exemplo, o fim do colonialismo e do absolutismo e implantação do liberalismo econômico, bem como a liberdade religiosa, o que culminou em movimentos como a <a href="revolucaoFrancesa.php">Revolução Francesa (1789)</a>.</p>



